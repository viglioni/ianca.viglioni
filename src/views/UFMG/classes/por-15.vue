<div class="outline-text-5" id="text-3-5-3-11">
<p>
Ficam sempre na <b>3ª pessoa do singular</b>.
</p>

<p>
<b>HAVER (no sentido de existir, ocorrer, tempo decorrido):</b>
</p>
<ul class="org-ul">
