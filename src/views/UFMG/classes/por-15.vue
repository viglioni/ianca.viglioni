<div id="outline-container-org1ca8fd0" class="outline-5">
<h5 id="org1ca8fd0"><span class="section-number-5">3.5.5.3.</span> 3. Anexo, Incluso, Mesmo, Próprio</h5>
<div class="outline-text-5" id="text-3-5-5-3">
<p>
<b>Concordam com o substantivo:</b>
</p>

<ul class="org-ul">
<li>A foto está <b>anexa</b>.</li>
<li>Os documentos estão <b>anexos</b>.</li>
