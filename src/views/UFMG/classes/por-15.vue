<p>
<b>Adjetivo (= metade):</b> Varia
</p>

<ul class="org-ul">
<li><b>Meia</b> garrafa.</li>
<li><b>Meios</b> estranhos.</li>
</ul>

<p>
<b>Advérbio (= um pouco):</b> Invariável
</p>

<ul class="org-ul">
<li>Ela está <b>meio</b> cansada. (um pouco cansada)</li>
<li>Elas estão <b>meio</b> tristes.</li>
</ul>
</div>
</div>

<div id="outline-container-org84f523f" class="outline-5">
<h5 id="org84f523f"><span class="section-number-5">3.5.5.6.</span> 6. Quite, Alerta</h5>
<div class="outline-text-5" id="text-3-5-5-6">
<p>
<b>Quite:</b> Varia
</p>
<ul class="org-ul">
<li>Estou <b>quite</b>.</li>
<li>Estamos <b>quites</b>.</li>
</ul>

<p>
<b>Alerta:</b> Invariável (advérbio)
</p>
<ul class="org-ul">
<li>Fiquem <b>alerta</b>.</li>
