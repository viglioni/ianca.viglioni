<div id="outline-container-orgdef609c" class="outline-4">
<h4 id="orgdef609c"><span class="section-number-4">3.5.4.</span> Concordância Nominal</h4>
<div class="outline-text-4" id="text-3-5-4">
<p>
<b>Regra geral:</b> Artigo, adjetivo, pronome e numeral concordam com o substantivo em gênero (masculino/feminino) e número (singular/plural).
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>O menino bonito</li>
<li>A menina bonita</li>
<li>Os meninos bonitos</li>
<li>As meninas bonitas</li>
</ul>
</div>
</div>

<div id="outline-container-orgba7fa5c" class="outline-4">
<h4 id="orgba7fa5c"><span class="section-number-4">3.5.5.</span> Casos Especiais de Concordância Nominal</h4>
<div class="outline-text-4" id="text-3-5-5">
</div>
<div id="outline-container-org890fc08" class="outline-5">
<h5 id="org890fc08"><span class="section-number-5">3.5.5.1.</span> 1. Adjetivo com Mais de um Substantivo</h5>
<div class="outline-text-5" id="text-3-5-5-1">
<p>
<b>Adjetivo ANTES:</b> Concorda com o mais <b>próximo</b>
</p>

<ul class="org-ul">
<li>Lindo dia e tarde.</li>
<li>Linda tarde e dia.</li>
</ul>

<p>
<b>Adjetivo DEPOIS:</b>
</p>

<ol class="org-ol">
<li><b>Plural</b> (concordância geral</li>
<li>mais comum)</li>

<li>Dia e tarde <b>lindos</b>.</li>

<li value="2"><b>Concorda com o mais próximo</b></li>

<li>Dia e tarde <b>linda</b>.</li>
</ol>

<p>
<b>Se um substantivo for feminino e outro masculino, adjetivo no masculino plural:</b>
</p>
<ul class="org-ul">
<li>A casa e o carro <b>novos</b>.</li>
</ul>
