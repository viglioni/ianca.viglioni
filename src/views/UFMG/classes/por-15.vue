</p>
<ul class="org-ul">
<li>Eu e tu <b>somos</b> amigos. (nós)</li>
<li>Eu e ele <b>somos</b> colegas. (nós)</li>
<li>Tu e Maria <b>sois/são</b> inteligentes. (vós/vocês)</li>
</ul>
