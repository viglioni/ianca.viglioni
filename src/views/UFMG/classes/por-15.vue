<li><b>Há</b> dois anos não o vejo. (tempo)</li>
</ul>

<p>
<b>FAZER (tempo decorrido, clima):</b>
</p>
<ul class="org-ul">
<li><b>Faz</b> dois meses que saí.</li>
<li><b>Faz</b> dias frios aqui.</li>
</ul>

<p>
<b>SER (horas, datas, distância):</b>
</p>
<ul class="org-ul">
