<div id="outline-container-org9883c8e" class="outline-5">
<h5 id="org9883c8e"><span class="section-number-5">3.5.3.2.</span> 2. Sujeito Composto com Pessoas Diferentes</h5>
<div class="outline-text-5" id="text-3-5-3-2">
<p>
<b>Prioridade:</b> 1ª pessoa &gt; 2ª pessoa &gt; 3ª pessoa
