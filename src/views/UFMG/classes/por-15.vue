</p>

<ul class="org-ul">
<li><b>Vendem-se</b> casas. (= casas são vendidas)</li>
<li><b>Aluga-se</b> apartamento. (= apartamento é alugado)</li>
<li><b>Consertam-se</b> relógios. (= relógios são consertados)</li>
</ul>
</div>
</div>

