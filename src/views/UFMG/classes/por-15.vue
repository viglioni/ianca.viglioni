<li>Casas <b>as mais confortáveis possíveis</b>.</li>
</ul>
</div>
</div>
</div>

<div id="outline-container-org4e480b1" class="outline-4">
<h4 id="org4e480b1"><span class="section-number-4">3.5.6.</span> Exercícios Resolvidos</h4>
<div class="outline-text-4" id="text-3-5-6">
</div>
<div id="outline-container-orgd661952" class="outline-5">
<h5 id="orgd661952"><span class="section-number-5">3.5.6.1.</span> Exercício 1</h5>
<div class="outline-text-5" id="text-3-5-6-1">
<p>
Complete com a forma correta:
</p>

<ol class="org-ol">
<li>A maioria dos alunos <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (chegar) cedo.</li>
<li>Mais de um candidato <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (desistir).</li>
<li><span class="underline"><span class="underline"><span class="underline">_</span></span></span> (Haver) muitas pessoas na festa.</li>
</ol>

<p>
<b>Resposta:</b> a) <b>chegou</b> ou <b>chegaram</b> (ambas corretas) b) <b>desistiu</b> (mais de um → singular) c) <b>Havia</b> (haver = existir → sempre singular)
</p>
</div>
</div>

<div id="outline-container-org81ffd81" class="outline-5">
<h5 id="org81ffd81"><span class="section-number-5">3.5.6.2.</span> Exercício 2</h5>
<div class="outline-text-5" id="text-3-5-6-2">
<p>
Corrija se necessário:
</p>

<ol class="org-ol">
<li>Fazem dois anos que não o vejo.</li>
<li>Houveram muitos problemas.</li>
<li>Podem haver soluções.</li>
</ol>

<p>
<b>Resposta:</b> a) <b>Faz</b> dois anos (fazer = tempo → impessoal, singular) b) <b>Houve</b> muitos problemas (haver = existir → impessoal, singular) c) <b>Pode haver</b> soluções (haver impessoal não varia; “poder” fica singular)
</p>
</div>
</div>

<div id="outline-container-org3e80622" class="outline-5">
<h5 id="org3e80622"><span class="section-number-5">3.5.6.3.</span> Exercício 3</h5>
<div class="outline-text-5" id="text-3-5-6-3">
<p>
Complete:
</p>

<ol class="org-ol">
<li>Vende-se casas / Vendem-se casas</li>
<li>Precisa-se de funcionários / Precisam-se de funcionários</li>
<li>Aluga-se apartamentos / Alugam-se apartamentos</li>
</ol>

<p>
<b>Resposta:</b> a) <b>Vendem-se</b> casas (VTD + se = voz passiva, concorda) b) <b>Precisa-se</b> de funcionários (VTI + se = indeterminado, singular) c) <b>Alugam-se</b> apartamentos (VTD + se = voz passiva, concorda)
</p>
</div>
</div>

<div id="outline-container-org894f1f9" class="outline-5">
<h5 id="org894f1f9"><span class="section-number-5">3.5.6.4.</span> Exercício 4</h5>
<div class="outline-text-5" id="text-3-5-6-4">
<p>
Concordância nominal:
</p>

<ol class="org-ol">
<li>A casa e o carro <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (novo).</li>
<li>Seguem <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (anexo) os documentos.</li>
<li>Ela está <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (meio) nervosa.</li>
</ol>

<p>
<b>Resposta:</b> a) <b>novos</b> (masculino plural quando há gêneros diferentes) b) <b>anexos</b> (concorda com “documentos”) c) <b>meio</b> (advérbio = um pouco → invariável)
</p>
</div>
</div>

<div id="outline-container-org7dcb05b" class="outline-5">
<h5 id="org7dcb05b"><span class="section-number-5">3.5.6.5.</span> Exercício 5</h5>
<div class="outline-text-5" id="text-3-5-6-5">
<p>
Complete:
</p>

<ol class="org-ol">
<li>É <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (proibido) entrada.</li>
<li>É <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (proibido) a entrada.</li>
<li>Elas <span class="underline"><span class="underline"><span class="underline">_</span></span></span> (mesmo) fizeram.</li>
</ol>

<p>
<b>Resposta:</b> a) <b>proibido</b> (sem artigo → invariável) b) <b>proibida</b> (com artigo → varia) c) <b>mesmas</b> (concorda com “elas”)
</p>
</div>
</div>
</div>

<div id="outline-container-org8bfad71" class="outline-4">
<h4 id="org8bfad71"><span class="section-number-4">3.5.7.</span> Dicas para a Prova</h4>
<div class="outline-text-4" id="text-3-5-7">
<ol class="org-ol">
<li><b>Sujeito composto antes do verbo:</b> Plural</li>
<li><b>Haver = existir:</b> Sempre singular (havia, houve, há)</li>
<li><b>Fazer = tempo:</b> Sempre singular (faz, fazia)</li>
<li><b>VTD + se:</b> Concorda (vendem-se casas)</li>
<li><b>VTI + se:</b> Singular (precisa-se de)</li>
<li><b>Meio (advérbio):</b> Invariável (meio cansada)</li>
<li><b>Anexo:</b> Varia (documentos anexos)</li>
<li><b>“É proibido” sem artigo:</b> Invariável</li>
<li><b>Mais de um:</b> Singular (mais de um saiu)</li>
<li><b>Menos de dois:</b> Plural (menos de dois metros)</li>
</ol>
</div>
</div>

<div id="outline-container-org2aba3fe" class="outline-4">
<h4 id="org2aba3fe"><span class="section-number-4">3.5.8.</span> Conceitos-Chave para Memorizar</h4>
<div class="outline-text-4" id="text-3-5-8">
<p>
<b>Concordância Verbal:</b>
</p>
<ul class="org-ul">
<li>Verbo concorda com sujeito em número e pessoa</li>
<li>Sujeito composto: plural</li>
<li>Verbos impessoais: 3ª singular (haver, fazer)</li>
<li>VTD + se: concorda; VTI + se: singular</li>
</ul>

<p>
<b>Concordância Nominal:</b>
</p>
<ul class="org-ul">
<li>Adjetivo concorda com substantivo</li>
<li>Anexo, incluso, mesmo, próprio: variam</li>
<li>Meio (advérbio): invariável</li>
<li>Bastante (advérbio): invariável</li>
<li>É proibido (sem artigo): invariável</li>
</ul>
</div>
</div>

<div id="outline-container-orgbf0ae56" class="outline-4">
<h4 id="orgbf0ae56"><span class="section-number-4">3.5.9.</span> Resumo</h4>
