<div id="outline-container-org5359ea9" class="outline-5">
<h5 id="org5359ea9"><span class="section-number-5">2.4.5.2.</span> Postulados de Bohr</h5>
<div class="outline-text-5" id="text-2-4-5-2">
<p>
<b>1. Órbitas Estacionárias (Níveis de Energia)</b>
</p>
<ul class="org-ul">
<li>Elétrons giram em <b>órbitas circulares</b> ao redor do núcleo</li>
<li>Essas órbitas têm <b>energias definidas</b> (quantizadas)</li>
<li>Enquanto na órbita, elétron <b>não</b> perde energia (órbita estacionária)</li>
</ul>

<p>
<b>2. Níveis de Energia (Camadas)</b>
</p>
<ul class="org-ul">
<li>Órbitas são chamadas de <b>níveis de energia</b> ou <b>camadas</b></li>
<li>Identificadas por números: n = 1, 2, 3, 4, 5, 6, 7&#x2026;</li>
<li>Ou por letras: K, L, M, N, O, P, Q</li>
</ul>

<pre class="example" id="orgc037fe6">
Camadas:  K   L   M   N   O   P   Q
Níveis:   1   2   3   4   5   6   7
Energia: baixa ───────────→ alta
</pre>

<p>
<b>3. Salto Quântico</b>
</p>
<ul class="org-ul">
<li>Elétron pode <b>saltar</b> de uma órbita para outra</li>
<li>Ao <b>absorver</b> energia: salta para nível mais externo (excitação)</li>
<li>Ao <b>emitir</b> energia: volta para nível mais interno (desexcitação)</li>
<li>Energia emitida ou absorvida é na forma de <b>fóton</b> (luz)</li>
</ul>

<p>
<b>Fórmula da energia do fóton:</b>
</p>

<pre class="example" id="org440e797">
ΔE = E_final
- E_inicial = hf

Onde:
- ΔE = variação de energia
- h = constante de Planck
- f = frequência da radiação
</pre>
</div>
</div>

