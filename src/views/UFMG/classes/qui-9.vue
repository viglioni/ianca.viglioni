<div id="outline-container-org0dc47c1" class="outline-5">
<h5 id="org0dc47c1"><span class="section-number-5">2.4.6.2.</span> Conceitos Principais</h5>
<div class="outline-text-5" id="text-2-4-6-2">
<p>
<b>1. Princípio da Incerteza de Heisenberg</b>
</p>
<ul class="org-ul">
<li>Impossível determinar simultaneamente posição e velocidade exatas do elétron</li>
<li>Elétron tem comportamento <b>dual</b>: onda e partícula</li>
</ul>

<p>
<b>2. Orbitais (Nuvens Eletrônicas)</b>
</p>
<ul class="org-ul">
<li>Não existem <b>órbitas</b> definidas</li>
<li>Existem <b>orbitais</b>: regiões de probabilidade de encontrar o elétron</li>
<li>Orbital = nuvem eletrônica onde é provável encontrar elétrons</li>
</ul>

<p>
<b>3. Números Quânticos</b> Quatro números descrevem cada elétron:
</p>

<p>
<b>n</b> = número quântico principal (camada, nível de energia)
</p>
<ul class="org-ul">
<li>n = 1, 2, 3, 4&#x2026;</li>
</ul>

<p>
<b>ℓ</b> = número quântico secundário (subcamada, formato do orbital)
</p>
<ul class="org-ul">
<li>ℓ = 0, 1, 2, 3&#x2026; (n-1)</li>
<li>s (ℓ=0), p (ℓ=1), d (ℓ=2), f (ℓ=3)</li>
</ul>

<p>
<b>m</b> = número quântico magnético (orientação do orbital)
</p>
<ul class="org-ul">
<li>m = -ℓ &#x2026; 0 &#x2026; +ℓ</li>
</ul>

<p>
<b>s</b> = número quântico spin (rotação do elétron)
</p>
<ul class="org-ul">
<li>s = +½ ou -½</li>
</ul>

<p>
<b>Formas dos orbitais:</b>
</p>
<ul class="org-ul">
<li><b>s:</b> esférico (1 orbital)</li>
<li><b>p:</b> halteres (3 orbitais)</li>
<li><b>d:</b> complexo (5 orbitais)</li>
<li><b>f:</b> muito complexo (7 orbitais)</li>
</ul>
</div>
</div>

