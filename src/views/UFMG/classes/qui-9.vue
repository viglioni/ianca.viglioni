<h4 id="orgc3ec4f1"><span class="section-number-4">2.4.10.</span> Dicas para a Prova</h4>
<div class="outline-text-4" id="text-2-4-10">
<ol class="org-ol">
<li><b>Ordem cronológica:</b> Dalton → Thomson → Rutherford → Bohr → Quântico</li>
<li><b>Dalton:</b> indivisível (errado depois)</li>
<li><b>Thomson:</b> descobriu o elétron, pudim de passas</li>
<li><b>Rutherford:</b> núcleo positivo, experimento da lâmina de ouro</li>
<li><b>Bohr:</b> níveis de energia, espectros atômicos</li>
<li><b>Quântico:</b> orbitais, nuvens eletrônicas</li>
<li><b>Camadas:</b> 2n² elétrons no máximo</li>
<li><b>Cada modelo melhorou o anterior</b>, não invalidou completamente</li>
</ol>
</div>
