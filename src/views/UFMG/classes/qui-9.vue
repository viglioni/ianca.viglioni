<div id="outline-container-orgc8c4095" class="outline-5">
<h5 id="orgc8c4095"><span class="section-number-5">2.4.5.1.</span> Contexto</h5>
<div class="outline-text-5" id="text-2-4-5-1">
<p>
Niels Bohr aperfeiçoou o modelo de Rutherford incorporando ideias da <b>física quântica</b>.
