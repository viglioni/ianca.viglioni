<div id="outline-container-org8b98cfe" class="outline-4">
<h4 id="org8b98cfe"><span class="section-number-4">2.4.11.</span> Conceitos-Chave para Memorizar</h4>
<div class="outline-text-4" id="text-2-4-11">
<p>
<b>Evolução:</b> Dalton → Thomson → Rutherford → Bohr → Quântico
</p>

<p>
<b>Cada modelo:</b>
</p>
<ul class="org-ul">
<li><b>Dalton:</b> bola de bilhar</li>
<li><b>Thomson:</b> pudim de passas (descobriu e⁻)</li>
<li><b>Rutherford:</b> núcleo + eletrosfera (lâmina de ouro)</li>
<li><b>Bohr:</b> níveis de energia (espectros)</li>
<li><b>Quântico:</b> orbitais (atual)</li>
</ul>

<p>
<b>Partículas:</b>
</p>
<ul class="org-ul">
<li><b>Próton:</b> carga +1, massa 1 u, núcleo</li>
<li><b>Nêutron:</b> carga 0, massa 1 u, núcleo</li>
<li><b>Elétron:</b> carga -1, massa ≈0, eletrosfera</li>
</ul>

<p>
<b>Camadas:</b>
</p>
<ul class="org-ul">
<li>K, L, M, N, O, P, Q</li>
<li>Máximo: 2n²</li>
</ul>
