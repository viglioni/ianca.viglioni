<div id="outline-container-orgf3bfb9a" class="outline-4">
<h4 id="orgf3bfb9a"><span class="section-number-4">5.2.1.</span> Objetivo</h4>
<div class="outline-text-4" id="text-5-2-1">
<p>
Desenvolver estratégias de leitura e interpretação para as questões de Português da prova.
</p>
</div>
</div>

<div id="outline-container-org3d16569" class="outline-4">
<h4 id="org3d16569"><span class="section-number-4">5.2.2.</span> PARTE 1: Leitura Estratégica</h4>
<div class="outline-text-4" id="text-5-2-2">
<p>
<b>Antes de ler:</b>
</p>

<pre class="example" id="org76a052f">
1. Leia o enunciado da questão ANTES do texto
2. Identifique o que está sendo pedido
3. Sublinhe palavras-chave no enunciado
</pre>

<p>
<b>Durante a leitura:</b>
</p>

<pre class="example" id="orge4dedd0">
1. Leia com atenção, mas não devagar demais
2. Identifique:
   - Tema central
   - Tese/argumento principal
   - Exemplos e dados de apoio
3. Sublinhe informações importantes
4. Anote ideias principais nas margens
</pre>

<p>
<b>Após a leitura:</b>
</p>

<pre class="example" id="orge2fd9ea">
1. Releia o enunciado
2. Volte ao texto para confirmar
3. Elimine alternativas claramente erradas
4. Compare as restantes com o texto
</pre>
</div>
</div>

<div id="outline-container-org5574aa4" class="outline-4">
<h4 id="org5574aa4"><span class="section-number-4">5.2.3.</span> PARTE 2: Tipos de Questões</h4>
<div class="outline-text-4" id="text-5-2-3">
<p>
<b>1. Identificação do tema/ideia principal</b>
</p>

<pre class="example" id="org56420fb">
Pergunta: "O texto trata principalmente de..."

Estratégia:
- Identificar o assunto recorrente
- Não confundir tema com exemplo específico
- Tema = ideia geral, não detalhe
</pre>

<p>
<b>2. Compreensão de fragmentos</b>
</p>

<pre class="example" id="orga19bdbb">
Pergunta: "No trecho '...', o autor sugere que..."

Estratégia:
- Ler parágrafos anteriores e posteriores (contexto)
- Atenção a palavras de transição (mas, porém, entretanto)
- Não inferir além do que está escrito
</pre>

<p>
<b>3. Identificação de recursos linguísticos</b>
</p>

<pre class="example" id="org1bcca37">
Pergunta: "O uso de aspas/metáfora/ironia tem a função de..."

Estratégia:
- Compreender a intenção do autor
- Recursos comuns:
  * Aspas: citação, destaque, ironia
  * Metáfora: comparação implícita
  * Ironia: dizer o contrário do que se pensa
</pre>

<p>
<b>4. Inferência</b>
</p>

<pre class="example" id="org8e8479f">
Pergunta: "Pode-se inferir/deduzir do texto que..."

Estratégia:
- Basear-se SEMPRE no texto
- Não usar conhecimento prévio para inferir
- Inferência deve ser lógica e apoiada no texto
</pre>

<p>
<b>5. Finalidade/Função do texto</b>
</p>

<pre class="example" id="org03f4ca6">
Pergunta: "O texto tem como objetivo..."

Estratégia:
- Identificar tipo textual:
  * Narrativo: contar história
  * Descritivo: descrever
  * Dissertativo: argumentar, defender tese
  * Injuntivo: instruir, orientar
</pre>
</div>
</div>

<div id="outline-container-org2fdeab3" class="outline-4">
<h4 id="org2fdeab3"><span class="section-number-4">5.2.4.</span> PARTE 3: Armadilhas Comuns</h4>
<div class="outline-text-4" id="text-5-2-4">
<p>
<b>Armadilha 1: Extrapolação</b>
</p>

<pre class="example" id="org07e94d9">
Alternativa vai além do que o texto diz
Usa informação que você conhece, mas não está no texto

Solução: Só marque se ESTIVER NO TEXTO
</pre>

<p>
<b>Armadilha 2: Redução</b>
</p>

<pre class="example" id="orgdd0ccfe">
Alternativa pega detalhe e apresenta como ideia principal

Solução: Ideia principal ≠ exemplo específico
</pre>

<p>
<b>Armadilha 3: Contradição</b>
</p>

<pre class="example" id="org821524a">
Alternativa diz o oposto do texto

Solução: Fácil de eliminar, leia com atenção
</pre>

<p>
<b>Armadilha 4: Distorção</b>
</p>

<pre class="example" id="org1939074">
Alternativa usa palavras do texto, mas muda o sentido

Solução: Não se deixe enganar por "palavras conhecidas"
Verifique se o SENTIDO está correto
</pre>
</div>
</div>

<div id="outline-container-org6a58b1f" class="outline-4">
<h4 id="org6a58b1f"><span class="section-number-4">5.2.5.</span> PARTE 4: Dicas de Gramática Rápida</h4>
<div class="outline-text-4" id="text-5-2-5">
<p>
<b>Concordância verbal:</b>
</p>

<pre class="example" id="orga8717fa">
Sujeito simples: verbo concorda
"O aluno estuda" ✓
"O aluno estudam" ✗

Sujeito composto (antes): verbo plural
"Maria e João estudam" ✓

Sujeito composto (depois): verbo singular ou plural
"Estuda Maria e João" ou "Estudam Maria e João" ✓
</pre>

<p>
<b>Concordância nominal:</b>
</p>

<pre class="example" id="org503021a">
Adjetivo concorda com substantivo
"Casa bonita" ✓
"Casas bonitas" ✓
</pre>

<p>
<b>Crase:</b>
</p>

<pre class="example" id="orgcf8e7d2">
a + a = à

Macete: troque por "ao"
Se ficar "ao", use "à"

"Vou à escola" → "Vou ao colégio" ✓
"Vou a escola" → "Vou ao colégio"? Não! (errado)
</pre>

<p>
<b>Pronomes relativos:</b>
</p>

<pre class="example" id="org33fe3be">
QUE: retoma substantivo
"O livro que li é bom"

ONDE: retoma lugar
"A cidade onde nasci"

CUJO: indica posse
"O autor cujo livro li"
</pre>
</div>
</div>

<div id="outline-container-orgca3ab71" class="outline-4">
<h4 id="orgca3ab71"><span class="section-number-4">5.2.6.</span> PARTE 5: Interpretação de Poemas</h4>
<div class="outline-text-4" id="text-5-2-6">
<p>
<b>Características:</b>
</p>

<pre class="example" id="org2c8c0bf">
- Linguagem conotativa (figurada)
- Figuras de linguagem
- Ritmo, rima, métrica
- Subjetividade
</pre>

<p>
<b>Estratégia:</b>
</p>

<pre class="example" id="orgc850efa">
1. Leia duas vezes (ritmo e sentido)
2. Identifique o eu-lírico (quem fala)
3. Identifique tema/sentimento
4. Atenção a figuras de linguagem:
   - Metáfora: comparação implícita
   - Personificação: atribuir características humanas
   - Hipérbole: exagero
</pre>
</div>
</div>

<div id="outline-container-org1ab6a59" class="outline-4">
<h4 id="org1ab6a59"><span class="section-number-4">5.2.7.</span> PARTE 6: Dicas Finais para a Prova</h4>
<div class="outline-text-4" id="text-5-2-7">
<p>
<b>Gestão de tempo:</b>
</p>

<pre class="example" id="orgacf9941">
- Não passe mais de 5 minutos por texto
- Se travar, pule e volte depois
- Deixe as questões de gramática por último (mais rápidas)
</pre>

<p>
<b>Técnica de eliminação:</b>
</p>

<pre class="example" id="orgf86318c">
1. Elimine alternativas absurdas (20 segundos)
2. Compare as 2-3 restantes com o texto
3. Escolha a mais completa e precisa
</pre>

<p>
<b>O que NUNCA fazer:</b>
</p>

<pre class="example" id="orgeccadbb">
✗ Marcar sem ler o texto
✗ Usar só conhecimento prévio
✗ Escolher por "achismo"
✗ Não reler trechos importantes
✗ Ignorar comandos da questão
</pre>

<p>
<b>O que SEMPRE fazer:</b>
</p>

<pre class="example" id="org89b32ca">
✓ Ler enunciado antes do texto
✓ Sublinhar informações-chave
✓ Voltar ao texto para confirmar
✓ Eliminar alternativas erradas
✓ Basear-se APENAS no texto
</pre>
</div>
</div>

<div id="outline-container-orgc9774b8" class="outline-4">
<h4 id="orgc9774b8"><span class="section-number-4">5.2.8.</span> Exemplo Prático</h4>
<div class="outline-text-4" id="text-5-2-8">
<p>
<b>Texto:</b> “A tecnologia transformou a comunicação humana. Hoje, é possível conversar com pessoas do outro lado do mundo em tempo real. No entanto, alguns especialistas alertam para o empobrecimento das relações face a face.”
</p>

<p>
<b>Questão:</b> O texto sugere que:
</p>

<ol class="org-ol">
<li>A tecnologia deve ser evitada</li>
<li>As relações presenciais estão sendo prejudicadas ✓</li>
<li>A comunicação global é impossível</li>
<li>Especialistas são contra o progresso</li>
</ol>

<p>
<b>Análise:</b>
</p>

<pre class="example" id="org40d60b3">
a) EXTRAPOLAÇÃO
- texto não diz para evitar
b) CORRETA
- "empobrecimento das relações face a face"
c) CONTRADIÇÃO
- texto diz que é possível
d) DISTORÇÃO
- não é contra progresso, alerta sobre efeito colateral
</pre>
</div>
</div>

