<div id="outline-container-org95e613a" class="outline-5">
<h5 id="org95e613a"><span class="section-number-5">4.5.7.3.</span> Exercício 3</h5>
<div class="outline-text-5" id="text-4-5-7-3">
<p>
Por que o Egito era chamado de “dádiva do Nilo”?
</p>

<p>
<b>Resposta:</b> Porque a civilização egípcia dependia totalmente do Rio Nilo. As <b>cheias periódicas</b> do rio fertilizavam o solo, permitindo a agricultura em meio ao deserto. Sem o Nilo, o Egito seria apenas deserto árido e inabitável.
</p>
</div>
</div>

