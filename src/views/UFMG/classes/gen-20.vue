</p>
<ul class="org-ul">
<li><b>Escrita hieroglífica</b> (sagrada)</li>
<li><b>Escrita hierática</b> (simplificada)</li>
<li><b>Escrita demótica</b> (popular)</li>
<li><b>Pedra de Roseta:</b> permitiu decifrar hieróglifos (Champollion)</li>
</ul>

<p>
<b>Arquitetura:</b>
</p>
<ul class="org-ul">
<li>Pirâmides (túmulos dos faraós)</li>
<li>Templos monumentais</li>
<li>Esfinges</li>
</ul>

<p>
<b>Ciências:</b>
</p>
<ul class="org-ul">
<li>Matemática (geometria, cálculos)</li>
<li>Medicina (mumificação → anatomia)</li>
<li>Astronomia (calendário de 365 dias)</li>
