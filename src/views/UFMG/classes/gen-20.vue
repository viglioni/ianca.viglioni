<p>
(UFMG adaptada) O Código de Hamurabi, criado na Mesopotâmia, é conhecido por estabelecer qual princípio?
</p>

<p>
<b>Resposta:</b> A <b>Lei de Talião</b>: “olho por olho, dente por dente”
</p>
<ul class="org-ul">
<li>princípio de que a punição deveria ser proporcional ao crime cometido.</li>
</ul>
