<div id="outline-container-org1acb098" class="outline-4">
<h4 id="org1acb098"><span class="section-number-4">4.3.6.</span> Reagente Limitante e Reagente em Excesso</h4>
<div class="outline-text-4" id="text-4-3-6">
<p>
<b>Reagente limitante:</b> aquele que é totalmente consumido primeiro, limitando a quantidade de produto
</p>

<p>
<b>Reagente em excesso:</b> sobra após a reação
</p>

<p>
<b>Como identificar:</b>
</p>

<p>
<b>1.</b> Calcule quantos mols de cada reagente você tem
</p>

<p>
<b>2.</b> Veja a proporção necessária pela equação
</p>

<p>
<b>3.</b> O que “falta” primeiro é o limitante
</p>

<p>
<b>Exemplo:</b>
</p>

<pre class="example" id="orgcb19b64">
2 H₂ + O₂ → 2 H₂O
</pre>

<p>
Temos: 3 mols de H₂ e 2 mols de O₂
</p>

<p>
Proporção necessária: 2 mol H₂ : 1 mol O₂
</p>

<p>
Para 3 mols de H₂, precisamos de 3/2 = 1,5 mols de O₂ Temos 2 mols de O₂ → <b>H₂ é limitante</b>
</p>

<p>
Ou: Para 2 mols de O₂, precisamos de 4 mols de H₂ Temos apenas 3 mols de H₂ → <b>H₂ é limitante</b>
</p>
</div>
</div>

