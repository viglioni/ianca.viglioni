</p>
<ul class="org-ul">
<li>massa ↔ mol: n = m/M</li>
<li>mol ↔ volume (CNTP): V = n × 22,4</li>
<li>mol ↔ partículas: N = n × 6,02×10²³</li>
</ul>

<p>
<b>Método:</b> 1. Balancear equação 2. Calcular M (massas molares) 3. Usar coeficientes como proporção 4. Regra de três
