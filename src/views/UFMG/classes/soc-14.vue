<h4 id="org2eadd9b"><span class="section-number-4">3.4.5.</span> Principais Métodos e Técnicas de Pesquisa</h4>
</div>
<div id="outline-container-orgacde028" class="outline-4">
<h4 id="orgacde028"><span class="section-number-4">3.4.6.</span> 1. Pesquisa Bibliográfica</h4>
<div class="outline-text-4" id="text-3-4-6">
<p>
<b>Definição:</b> Análise de material já publicado (livros, artigos, teses).
</p>

<p>
<b>Quando usar:</b>
</p>
<ul class="org-ul">
<li>Base para qualquer pesquisa</li>
<li>Conhecer o estado da arte</li>
<li>Fundamentação teórica</li>
</ul>

<p>
<b>Vantagens:</b>
</p>
<ul class="org-ul">
<li>Acesso a grande volume de informação</li>
<li>Baixo custo</li>
<li>Visão ampla do tema</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Dependência da qualidade das fontes</li>
<li>Dados podem estar desatualizados</li>
</ul>
</div>
</div>

<div id="outline-container-orgb2d8ea4" class="outline-4">
<h4 id="orgb2d8ea4"><span class="section-number-4">3.4.7.</span> 2. Pesquisa Documental</h4>
<div class="outline-text-4" id="text-3-4-7">
<p>
<b>Definição:</b> Análise de documentos que não receberam tratamento analítico.
</p>

<p>
<b>Fontes:</b>
</p>
<ul class="org-ul">
<li>Documentos oficiais (leis, decretos)</li>
<li>Relatórios institucionais</li>
<li>Cartas, diários</li>
<li>Fotografias, vídeos</li>
<li>Registros estatísticos</li>
</ul>

<p>
<b>Diferença da bibliográfica:</b> Documentos sem análise prévia vs publicações acadêmicas analisadas.
</p>
</div>
</div>

<div id="outline-container-orgd915bc6" class="outline-4">
<h4 id="orgd915bc6"><span class="section-number-4">3.4.8.</span> 3. Observação</h4>
<div class="outline-text-4" id="text-3-4-8">
<p>
<b>Definição:</b> Técnica de coleta de dados através da observação direta de comportamentos e situações.
</p>
</div>

<div id="outline-container-orge7a5891" class="outline-5">
<h5 id="orge7a5891"><span class="section-number-5">3.4.8.1.</span> Observação Participante</h5>
<div class="outline-text-5" id="text-3-4-8-1">
<ul class="org-ul">
<li>Pesquisador <b>integra-se</b> ao grupo estudado</li>
<li>Vivência direta da realidade</li>
<li>Antropologia e etnografia</li>
</ul>

<p>
<b>Exemplo:</b> Pesquisador convive com comunidade indígena para estudar seus costumes.
</p>

<p>
<b>Vantagens:</b>
</p>
<ul class="org-ul">
<li>Compreensão profunda</li>
<li>Acesso a informações não verbalizadas</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Risco de perder objetividade</li>
<li>Demorado</li>
<li>Difícil generalização</li>
</ul>
</div>
</div>

<div id="outline-container-org29c53f5" class="outline-5">
<h5 id="org29c53f5"><span class="section-number-5">3.4.8.2.</span> Observação Não-Participante</h5>
<div class="outline-text-5" id="text-3-4-8-2">
<ul class="org-ul">
<li>Pesquisador <b>observa sem participar</b></li>
<li>Mantém distanciamento</li>
<li>Menos interferência</li>
</ul>

<p>
<b>Exemplo:</b> Observar interações em sala de aula sem se envolver.
</p>
</div>
</div>

<div id="outline-container-org4da5707" class="outline-5">
<h5 id="org4da5707"><span class="section-number-5">3.4.8.3.</span> Observação Sistemática</h5>
<div class="outline-text-5" id="text-3-4-8-3">
<ul class="org-ul">
<li>Estruturada e planejada</li>
<li>Uso de protocolos de observação</li>
<li>Mais objetiva</li>
</ul>
</div>
</div>

<div id="outline-container-orgce00169" class="outline-5">
<h5 id="orgce00169"><span class="section-number-5">3.4.8.4.</span> Observação Assistemática</h5>
<div class="outline-text-5" id="text-3-4-8-4">
<ul class="org-ul">
<li>Livre, sem roteiro rígido</li>
<li>Exploratória</li>
<li>Mais flexível</li>
</ul>
</div>
</div>
</div>

<div id="outline-container-orge969df0" class="outline-4">
<h4 id="orge969df0"><span class="section-number-4">3.4.9.</span> 4. Entrevista</h4>
<div class="outline-text-4" id="text-3-4-9">
<p>
<b>Definição:</b> Coleta de dados através de diálogo direto entre pesquisador e entrevistado.
</p>
</div>

<div id="outline-container-org1148443" class="outline-5">
<h5 id="org1148443"><span class="section-number-5">3.4.9.1.</span> Entrevista Estruturada</h5>
<div class="outline-text-5" id="text-3-4-9-1">
<ul class="org-ul">
<li>Roteiro fixo de perguntas</li>
<li>Padronizada</li>
<li>Facilita comparação</li>
<li>Mais objetiva</li>
</ul>

<p>
<b>Exemplo:</b> Questionário com perguntas fechadas aplicado da mesma forma a todos.
</p>
</div>
</div>

<div id="outline-container-org6382f34" class="outline-5">
<h5 id="org6382f34"><span class="section-number-5">3.4.9.2.</span> Entrevista Semiestruturada</h5>
<div class="outline-text-5" id="text-3-4-9-2">
<ul class="org-ul">
<li>Roteiro flexível</li>
<li>Permite aprofundamento</li>
<li>Combina perguntas abertas e fechadas</li>
<li><b>Mais comum em pesquisas qualitativas</b></li>
</ul>
</div>
</div>

<div id="outline-container-org2589357" class="outline-5">
<h5 id="org2589357"><span class="section-number-5">3.4.9.3.</span> Entrevista Não-Estruturada (Livre)</h5>
<div class="outline-text-5" id="text-3-4-9-3">
<ul class="org-ul">
<li>Sem roteiro rígido</li>
<li>Conversa informal</li>
<li>Exploratória</li>
<li>Muito flexível</li>
</ul>

<p>
<b>Vantagens das entrevistas:</b>
</p>
<ul class="org-ul">
<li>Informações detalhadas</li>
<li>Esclarecimento de dúvidas imediato</li>
<li>Adaptação ao entrevistado</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Demorado</li>
<li>Custo alto</li>
<li>Risco de viés do entrevistador</li>
</ul>
</div>
</div>
</div>

<div id="outline-container-org1ea0aad" class="outline-4">
<h4 id="org1ea0aad"><span class="section-number-4">3.4.10.</span> 5. Questionário</h4>
<div class="outline-text-4" id="text-3-4-10">
<p>
<b>Definição:</b> Instrumento com perguntas escritas para autopreenchimento.
</p>

<p>
<b>Tipos de perguntas:</b>
</p>

<p>
<b>Fechadas:</b>
</p>
<ul class="org-ul">
<li>Alternativas predefinidas</li>
<li>Fácil tabulação</li>
<li>Análise quantitativa</li>
<li><b>Exemplo:</b> Qual sua faixa etária? ( ) 18-25 ( ) 26-35 ( ) 36-45</li>
</ul>

<p>
<b>Abertas:</b>
</p>
<ul class="org-ul">
<li>Resposta livre</li>
<li>Análise qualitativa</li>
<li>Informação detalhada</li>
<li><b>Exemplo:</b> O que você pensa sobre educação pública?</li>
</ul>

<p>
<b>Vantagens:</b>
</p>
<ul class="org-ul">
<li>Alcance grande (especialmente online)</li>
<li>Baixo custo</li>
<li>Anonimato (respostas mais sinceras)</li>
<li>Padronização</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Taxa de retorno pode ser baixa</li>
<li>Sem esclarecimento de dúvidas</li>
<li>Impossível aprofundar</li>
</ul>
</div>
</div>

<div id="outline-container-orgb39b606" class="outline-4">
<h4 id="orgb39b606"><span class="section-number-4">3.4.11.</span> 6. Estudo de Caso</h4>
<div class="outline-text-4" id="text-3-4-11">
<p>
<b>Definição:</b> Investigação profunda de um caso específico (indivíduo, grupo, comunidade, organização).
</p>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Foco em um caso particular</li>
<li>Análise detalhada e contextualizada</li>
<li>Múltiplas fontes de dados</li>
</ul>

<p>
<b>Exemplo:</b> Estudo sobre uma escola específica que implementou método pedagógico inovador.
</p>

<p>
<b>Vantagens:</b>
</p>
<ul class="org-ul">
<li>Compreensão profunda</li>
<li>Riqueza de detalhes</li>
<li>Flexibilidade</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Difícil generalização</li>
<li>Risco de viés</li>
<li>Demorado</li>
</ul>
</div>
</div>

<div id="outline-container-org83c3f3d" class="outline-4">
<h4 id="org83c3f3d"><span class="section-number-4">3.4.12.</span> 7. Pesquisa de Campo (Survey)</h4>
<div class="outline-text-4" id="text-3-4-12">
<p>
<b>Definição:</b> Coleta de dados diretamente no local onde ocorre o fenômeno.
</p>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Contato direto com a realidade</li>
<li>Dados primários</li>
<li>Geralmente quantitativa</li>
</ul>

<p>
<b>Exemplo:</b> Levantamento sobre condições de trabalho em fábricas de determinada região.
</p>
</div>
</div>

<div id="outline-container-orgafca867" class="outline-4">
<h4 id="orgafca867"><span class="section-number-4">3.4.13.</span> 8. Pesquisa Experimental</h4>
<div class="outline-text-4" id="text-3-4-13">
<p>
<b>Definição:</b> Manipulação de variáveis para verificar relações de causa e efeito.
</p>

<p>
<b>Menos comum em Sociologia</b> (mais em Psicologia), mas possível.
</p>

<p>
<b>Exemplo:</b> Estudo sobre como diferentes formatos de mensagem influenciam comportamento de voto.
</p>
</div>
</div>

<div id="outline-container-org6136e10" class="outline-4">
<h4 id="org6136e10"><span class="section-number-4">3.4.14.</span> 9. História de Vida</h4>
<div class="outline-text-4" id="text-3-4-14">
<p>
<b>Definição:</b> Narrativa biográfica a partir do relato do próprio sujeito.
</p>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Qualitativa</li>
<li>Trajetória individual em contexto social</li>
<li>Memória e experiência</li>
</ul>

<p>
<b>Exemplo:</b> Histórias de vida de migrantes para entender processos migratórios.
</p>
</div>
</div>

<div id="outline-container-orgba85883" class="outline-4">
<h4 id="orgba85883"><span class="section-number-4">3.4.15.</span> 10. Grupos Focais (Focus Group)</h4>
<div class="outline-text-4" id="text-3-4-15">
<p>
<b>Definição:</b> Discussão em grupo sobre tema específico, mediada pelo pesquisador.
</p>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>6-12 participantes</li>
<li>Interação entre participantes</li>
<li>Diversidade de opiniões</li>
<li>Qualitativa</li>
</ul>

<p>
<b>Exemplo:</b> Grupo focal com jovens para discutir percepções sobre violência urbana.
</p>

<p>
<b>Vantagens:</b>
</p>
<ul class="org-ul">
<li>Interação gera insights</li>
<li>Rápido</li>
<li>Custo menor que entrevistas individuais</li>
</ul>

<p>
<b>Desvantagens:</b>
</p>
<ul class="org-ul">
<li>Pode haver dominância de alguns participantes</li>
<li>Dificuldade de generalização</li>
</ul>
</div>
</div>

<div id="outline-container-org1ff201c" class="outline-4">
<h4 id="org1ff201c"><span class="section-number-4">3.4.16.</span> Amostragem</h4>
<div class="outline-text-4" id="text-3-4-16">
<p>
<b>Amostra:</b> Subconjunto da população total que será efetivamente estudado.
</p>

<p>
<b>População (universo):</b> Totalidade de indivíduos/elementos que se quer estudar.
</p>
</div>

<div id="outline-container-org44a8405" class="outline-5">
<h5 id="org44a8405"><span class="section-number-5">3.4.16.1.</span> Tipos de Amostragem</h5>
<div class="outline-text-5" id="text-3-4-16-1">
<p>
<b>Probabilística (Aleatória):</b>
</p>
<ul class="org-ul">
<li>Todos têm chance conhecida de serem selecionados</li>
<li>Permite generalização estatística</li>
<li>Mais rigorosa</li>
</ul>

<p>
<b>Tipos:</b>
</p>
<ul class="org-ul">
<li><b>Aleatória simples:</b> Sorteio</li>
<li><b>Estratificada:</b> Divisão em estratos (ex: por idade, renda)</li>
<li><b>Por conglomerados:</b> Grupos (ex: escolas, bairros)</li>
</ul>

<p>
<b>Não-probabilística:</b>
</p>
<ul class="org-ul">
<li>Seleção intencional ou por conveniência</li>
<li>Não permite generalização estatística</li>
<li>Comum em pesquisas qualitativas</li>
</ul>

<p>
<b>Tipos:</b>
</p>
<ul class="org-ul">
<li><b>Por conveniência:</b> Mais acessíveis</li>
<li><b>Intencional (proposital):</b> Escolha baseada em critérios</li>
<li><b>Bola de neve:</b> Indicação de participantes por outros participantes</li>
</ul>
</div>
</div>
</div>

<div id="outline-container-org5cf35e9" class="outline-4">
<h4 id="org5cf35e9"><span class="section-number-4">3.4.17.</span> Ética na Pesquisa</h4>
<div class="outline-text-4" id="text-3-4-17">
<p>
<b>Princípios fundamentais:</b>
</p>

<ol class="org-ol">
<li><b>Consentimento Informado:</b> Participantes devem concordar voluntariamente</li>
<li><b>Confidencialidade:</b> Proteger identidade dos participantes</li>
<li><b>Anonimato:</b> Quando possível, não identificar participantes</li>
<li><b>Não maleficência:</b> Não causar danos</li>
<li><b>Beneficência:</b> Pesquisa deve trazer benefícios</li>
<li><b>Veracidade:</b> Honestidade nos dados e resultados</li>
</ol>

<p>
<b>Comitê de Ética em Pesquisa (CEP):</b>
</p>
<ul class="org-ul">
<li>Avalia projetos de pesquisa</li>
<li>Garante direitos dos participantes</li>
</ul>
</div>
</div>

<div id="outline-container-orga4c7ee3" class="outline-4">
<h4 id="orga4c7ee3"><span class="section-number-4">3.4.18.</span> Exemplos Práticos de Aplicação</h4>
<div class="outline-text-4" id="text-3-4-18">
