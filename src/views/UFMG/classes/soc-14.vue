<div id="outline-container-org81d97ee" class="outline-5">
<h5 id="org81d97ee"><span class="section-number-5">3.4.3.1.</span> 1. Quanto aos Objetivos</h5>
<div class="outline-text-5" id="text-3-4-3-1">
<p>
<b>Pesquisa Exploratória:</b>
</p>
<ul class="org-ul">
<li>Primeiro contato com o tema</li>
<li>Familiarização com o objeto de estudo</li>
<li>Geralmente qualitativa</li>
<li><b>Exemplo:</b> Estudo inicial sobre uso de redes sociais por idosos</li>
</ul>

<p>
<b>Pesquisa Descritiva:</b>
</p>
<ul class="org-ul">
<li>Descreve características de um fenômeno</li>
<li>Identifica relações entre variáveis</li>
<li><b>Exemplo:</b> Levantamento sobre perfil socioeconômico de estudantes universitários</li>
</ul>

<p>
<b>Pesquisa Explicativa:</b>
</p>
<ul class="org-ul">
<li>Busca explicar causas e consequências</li>
<li>Identifica fatores determinantes</li>
<li><b>Exemplo:</b> Análise das causas da evasão escolar em determinada região</li>
</ul>
</div>
</div>

<div id="outline-container-orgfd1bdd5" class="outline-5">
<h5 id="orgfd1bdd5"><span class="section-number-5">3.4.3.2.</span> 2. Quanto à Abordagem</h5>
<div class="outline-text-5" id="text-3-4-3-2">
<p>
<b>Pesquisa Qualitativa:</b>
</p>
<ul class="org-ul">
<li>Foco na compreensão profunda</li>
<li>Análise de significados, motivações, valores</li>
<li>Amostras menores</li>
<li>Dados não numéricos</li>
<li>Interpretação subjetiva</li>
</ul>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Subjetiva</li>
<li>Descritiva</li>
<li>Processo indutivo (do particular para o geral)</li>
<li>Holística (visão do todo)</li>
</ul>

<p>
<b>Pesquisa Quantitativa:</b>
</p>
<ul class="org-ul">
<li>Foco em dados numéricos e estatísticos</li>
<li>Mensurável e objetivo</li>
<li>Amostras grandes</li>
<li>Análise estatística</li>
<li>Generalização de resultados</li>
</ul>

<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Objetiva</li>
<li>Numérica</li>
<li>Processo dedutivo (do geral para o particular)</li>
<li>Verificação de hipóteses</li>
</ul>
</div>
</div>
