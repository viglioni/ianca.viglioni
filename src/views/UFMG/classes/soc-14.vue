<div id="outline-container-org01c3e29" class="outline-4">
<h4 id="org01c3e29"><span class="section-number-4">3.4.21.</span> Dicas para a Prova</h4>
<div class="outline-text-4" id="text-3-4-21">
<ol class="org-ol">
<li><b>Qualitativa vs Quantitativa:</b>

<ul class="org-ul">
<li>Qualitativa: compreensão, significados, “por quê?”</li>
<li>Quantitativa: mensuração, números, “quanto?”</li>
</ul></li>

<li><b>Observação participante:</b> Pesquisador integra-se ao grupo</li>

<li><b>Entrevista vs Questionário:</b>

<ul class="org-ul">
<li>Entrevista: diálogo direto</li>
<li>Questionário: autopreenchimento</li>
</ul></li>

<li><b>Amostragem:</b>

<ul class="org-ul">
<li>Probabilística: generalização possível</li>
<li>Não-probabilística: qualitativa, intencional</li>
</ul></li>

<li><b>Ética:</b> Consentimento, confidencialidade, não causar danos</li>

<li><b>Métodos mistos:</b> Combinam quali e quanti</li>
</ol>
</div>
</div>

<div id="outline-container-org171737b" class="outline-4">
<h4 id="org171737b"><span class="section-number-4">3.4.22.</span> Conceitos-Chave para Memorizar</h4>
<div class="outline-text-4" id="text-3-4-22">
<p>
<b>Abordagens:</b>
</p>
<ul class="org-ul">
<li><b>Qualitativa:</b> compreensão, interpretação, subjetiva</li>
<li><b>Quantitativa:</b> mensuração, estatística, objetiva</li>
</ul>

<p>
<b>Métodos principais:</b>
</p>
<ul class="org-ul">
<li>Observação (participante/não-participante)</li>
<li>Entrevista (estruturada/semiestruturada/livre)</li>
<li>Questionário (survey)</li>
<li>Estudo de caso</li>
<li>Grupos focais</li>
<li>História de vida</li>
</ul>

<p>
<b>Tipos de pesquisa:</b>
</p>
<ul class="org-ul">
<li>Exploratória: familiarização</li>
<li>Descritiva: descrever</li>
<li>Explicativa: explicar causas</li>
</ul>

<p>
<b>Amostragem:</b>
</p>
<ul class="org-ul">
<li>Probabilística: aleatória, representativa</li>
<li>Não-probabilística: intencional, conveniência</li>
</ul>
