<h4 id="org7961f58"><span class="section-number-4">7.2.8.</span> Questão 6</h4>
<div class="outline-text-4" id="text-7-2-8">
<ul class="org-ul">
<li>Estequiometria Integrada (Difícil)</li>
</ul>
<p>
<b>Enunciado:</b>
</p>

<p>
Na reação de combustão completa do metano (CH₄):
</p>

<p>
CH₄ + 2 O₂ → CO₂ + 2 H₂O
</p>

<p>
Dados: C = 12 g/mol, H = 1 g/mol, O = 16 g/mol
</p>

<ol class="org-ol">
<li>Calcule a massa molar de cada substância.</li>
<li>Quantos gramas de CO₂ são produzidos a partir de 32 g de CH₄?</li>
<li>Qual o volume de CO₂ produzido nas CNTP?</li>
</ol>

<hr />

<p>
<b>Resolução Completa:</b>
</p>

<p>
<b>a) Massas molares</b>
</p>

<p>
<b>CH₄:</b>
</p>

<pre class="example" id="orgd631af7">
M(CH₄) = 1×C + 4×H = 1×12 + 4×1 = 16 g/mol
</pre>

<p>
<b>O₂:</b>
</p>

<pre class="example" id="org32fa051">
M(O₂) = 2×O = 2×16 = 32 g/mol
</pre>

<p>
<b>CO₂:</b>
</p>

<pre class="example" id="org46ad3c8">
M(CO₂) = 1×C + 2×O = 1×12 + 2×16 = 44 g/mol
</pre>

<p>
<b>H₂O:</b>
</p>

<pre class="example" id="orgebc08bf">
M(H₂O) = 2×H + 1×O = 2×1 + 1×16 = 18 g/mol
</pre>

<p>
<b>Resposta a:</b>
</p>
<ul class="org-ul">
<li>M(CH₄) = 16 g/mol</li>
<li>M(O₂) = 32 g/mol</li>
<li>M(CO₂) = 44 g/mol</li>
<li>M(H₂O) = 18 g/mol</li>
</ul>

<p>
<b>b) Massa de CO₂ produzida</b>
</p>

<p>
<b>Proporção estequiométrica:</b>
</p>

<pre class="example" id="org19b5be6">
CH₄  :  CO₂
1 mol : 1 mol
</pre>

<p>
<b>Em massa:</b>
</p>

<pre class="example" id="org72e51b0">
16 g CH₄ ────► 44 g CO₂
32 g CH₄ ────► x
</pre>

<p>
<b>Regra de três:</b>
</p>

<pre class="example" id="org3e94e29">
16/32 = 44/x
16x = 32 × 44
16x = 1408
x = 88 g
</pre>

<p>
<b>Ou por mols:</b>
</p>

<pre class="example" id="orgab2c5bd">
n(CH₄) = m/M = 32/16 = 2 mol

Proporção: 1 mol CH₄ → 1 mol CO₂
Logo: 2 mol CH₄ → 2 mol CO₂

m(CO₂) = n × M = 2 × 44 = 88 g
</pre>

<p>
<b>Resposta b):</b> 88 g de CO₂
</p>

<p>
<b>c) Volume de CO₂ nas CNTP</b>
</p>

<pre class="example" id="org514a005">
n(CO₂) = 2 mol (calculado acima)

V = n × 22,4 L/mol (CNTP)
V = 2 × 22,4 = 44,8 L
</pre>

<p>
<b>Resposta c):</b> 44,8 L de CO₂
</p>

