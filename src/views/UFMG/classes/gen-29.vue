<h5 id="org703a2ed"><span class="section-number-5">6.4.9.1.</span> Exercício 1</h5>
<div class="outline-text-5" id="text-6-4-9-1">
<p>
Compare a democracia ateniense com as democracias modernas.
</p>

<p>
<b>Resposta:</b> <b>Semelhanças:</b> participação popular, igualdade perante a lei.
</p>

<p>
<b>Diferenças:</b>
</p>
<ul class="org-ul">
<li><b>Atenas:</b> democracia <b>direta</b> (cidadãos votam leis diretamente); <b>restrita</b> (só homens livres atenienses, ~10%)</li>
<li><b>Moderna:</b> democracia <b>representativa</b> (eleição de representantes); <b>universal</b> (todos adultos)</li>
</ul>
</div>
</div>

<div id="outline-container-orgd6e5400" class="outline-5">
<h5 id="orgd6e5400"><span class="section-number-5">6.4.9.2.</span> Exercício 2</h5>
<div class="outline-text-5" id="text-6-4-9-2">
<p>
Por que Esparta era considerada uma sociedade militarista?
</p>

<p>
<b>Resposta:</b> Toda a sociedade espartana girava em torno da guerra: educação militar obrigatória desde os 7 anos (ágoge), cidadãos eram guerreiros em tempo integral, Estado controlava a vida dos cidadãos para manter superioridade militar, necessário para controlar os hilotas (servos, maioria oprimida).
</p>
</div>
</div>

<div id="outline-container-org87ee117" class="outline-5">
<h5 id="org87ee117"><span class="section-number-5">6.4.9.3.</span> Exercício 3</h5>
<div class="outline-text-5" id="text-6-4-9-3">
<p>
(UFMG) Qual a importância da Lei das XII Tábuas para a República Romana?
</p>

<p>
<b>Resposta:</b> Foi a primeira codificação das leis romanas por escrito (450 a.C.), conquista dos plebeus. Antes, as leis eram orais e conhecidas apenas pelos patrícios, que as interpretavam arbitrariamente. A escrita tornou as leis públicas e conhecidas, garantindo maior igualdade jurídica.
</p>
</div>
</div>

<div id="outline-container-orgb28d108" class="outline-5">
<h5 id="orgb28d108"><span class="section-number-5">6.4.9.4.</span> Exercício 4</h5>
<div class="outline-text-5" id="text-6-4-9-4">
<p>
Explique a expressão “Roma: do Mediterrâneo ao Mare Nostrum”.
</p>

<p>
<b>Resposta:</b> <b>Mare Nostrum</b> (“Nosso Mar” em latim) era como os romanos chamavam o Mar Mediterrâneo após dominá-lo completamente. Reflete a expansão romana que transformou o Mediterrâneo em um “lago romano”, com todas as suas margens sob controle de Roma.
</p>
</div>
</div>
</div>

