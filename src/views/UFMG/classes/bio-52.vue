<li>Deficiência → bócio</li>
<li>Fonte: sal iodado, peixes</li>
</ul>

<p>
<b>Magnésio (Mg):</b>
</p>
<ul class="org-ul">
<li>Clorofila (plantas)</li>
<li>Cofator enzimático</li>
<li>Fontes: vegetais verdes, cereais</li>
</ul>
