<div id="outline-container-orgf2d8c90" class="outline-4">
<h4 id="orgf2d8c90"><span class="section-number-4">3.4.4.</span> 1. Carboidratos (Glicídios)</h4>
<div class="outline-text-4" id="text-3-4-4">
<p>
<b>Fórmula geral:</b> (CH₂O)_n ou C_nH₂nO_n
</p>

<p>
<b>Funções:</b>
</p>
<ul class="org-ul">
<li><b>Energética:</b> principal fonte de energia (glicose)</li>
<li><b>Estrutural:</b> celulose (parede celular vegetal), quitina (exoesqueleto)</li>
</ul>

<p>
<b>Classificação:</b>
</p>

<p>
<b>Monossacarídeos (açúcares simples):</b>
</p>
<ul class="org-ul">
<li>Glicose (C₆H₁₂O₆)</li>
<li>principal combustível celular</li>
<li>Frutose</li>
<li>açúcar das frutas</li>
<li>Ribose e Desoxirribose</li>
<li>RNA e DNA</li>
</ul>

<p>
<b>Dissacarídeos (2 monossacarídeos):</b>
</p>
<ul class="org-ul">
<li>Sacarose = glicose + frutose (açúcar de mesa)</li>
<li>Lactose = glicose + galactose (leite)</li>
<li>Maltose = glicose + glicose (malte)</li>
</ul>

<p>
<b>Polissacarídeos (muitos monossacarídeos):</b>
</p>
<ul class="org-ul">
<li><b>Amido:</b> reserva energética vegetal (batata, arroz)</li>
<li><b>Glicogênio:</b> reserva energética animal (fígado, músculos)</li>
<li><b>Celulose:</b> estrutural vegetal (não digerimos!)</li>
</ul>

<p>
<b>Fontes:</b>
</p>
<ul class="org-ul">
<li>Pães, massas, arroz, batata, frutas, doces</li>
</ul>
</div>
</div>

<div id="outline-container-org07fc3e9" class="outline-4">
<h4 id="org07fc3e9"><span class="section-number-4">3.4.5.</span> 2. Lipídios (Gorduras)</h4>
<div class="outline-text-4" id="text-3-4-5">
<p>
<b>Características:</b>
</p>
<ul class="org-ul">
<li>Insolúveis em água (apolares)</li>
<li>Solúveis em solventes orgânicos</li>
</ul>

<p>
<b>Funções:</b>
</p>
<ul class="org-ul">
<li><b>Energética:</b> reserva de energia (mais que carboidratos!)</li>
<li><b>Estrutural:</b> membranas celulares (fosfolipídios)</li>
<li><b>Isolamento térmico:</b> gordura subcutânea</li>
<li><b>Proteção:</b> amortecimento de órgãos</li>
<li><b>Hormonal:</b> hormônios esteroides (testosterona, estrogênio)</li>
</ul>

<p>
<b>Tipos:</b>
</p>

<p>
<b>Triglicerídeos:</b>
</p>
<ul class="org-ul">
<li>Gorduras e óleos</li>
<li>Formados por glicerol + 3 ácidos graxos</li>
<li>Reserva energética</li>
</ul>

<p>
<b>Fosfolipídios:</b>
</p>
<ul class="org-ul">
<li>Membrana celular</li>
<li>Cabeça polar + caudas apolares</li>
</ul>

<p>
<b>Esteroides:</b>
</p>
<ul class="org-ul">
<li>Colesterol (precursor de hormônios)</li>
<li>Hormônios sexuais</li>
<li>Vitamina D</li>
</ul>

<p>
<b>Ceras:</b>
</p>
<ul class="org-ul">
<li>Impermeabilização (folhas, ouvido)</li>
</ul>

<p>
<b>Fontes:</b>
</p>
<ul class="org-ul">
<li>Óleos vegetais, manteiga, carnes gordas, abacate, oleaginosas</li>
</ul>
</div>
</div>

<div id="outline-container-org7f3e9e3" class="outline-4">
<h4 id="org7f3e9e3"><span class="section-number-4">3.4.6.</span> 3. Proteínas</h4>
<div class="outline-text-4" id="text-3-4-6">
<p>
<b>Formadas por:</b> aminoácidos ligados por ligações peptídicas
</p>

<p>
<b>20 aminoácidos diferentes</b> combinados formam proteínas variadas
</p>

<p>
<b>Funções (as mais versáteis!):</b>
</p>

<p>
<b>Estrutural:</b>
</p>
<ul class="org-ul">
<li>Colágeno (pele, tendões)</li>
<li>Queratina (cabelo, unhas)</li>
<li>Actina e miosina (músculos)</li>
</ul>

<p>
<b>Enzimática:</b>
</p>
<ul class="org-ul">
<li>Catalisam reações (aceleram)</li>
<li>Ex: amilase, lipase, DNA polimerase</li>
</ul>

<p>
<b>Transporte:</b>
</p>
<ul class="org-ul">
<li>Hemoglobina (O₂ no sangue)</li>
<li>Mioglobina (O₂ nos músculos)</li>
</ul>

<p>
<b>Defesa:</b>
</p>
<ul class="org-ul">
<li>Anticorpos (imunoglobulinas)</li>
<li>Fibrinogênio (coagulação)</li>
</ul>

<p>
<b>Hormonal:</b>
</p>
<ul class="org-ul">
<li>Insulina (regula glicose)</li>
<li>Glucagon, hormônio do crescimento</li>
</ul>

<p>
<b>Contrátil:</b>
</p>
<ul class="org-ul">
<li>Movimento muscular</li>
</ul>

<p>
<b>Níveis de organização:</b>
</p>
<ul class="org-ul">
<li><b>Primária:</b> sequência de aminoácidos</li>
<li><b>Secundária:</b> hélice alfa, folha beta</li>
<li><b>Terciária:</b> dobramento 3D</li>
<li><b>Quaternária:</b> várias cadeias unidas</li>
</ul>

<p>
<b>Desnaturação:</b>
</p>
<ul class="org-ul">
<li>Perda da estrutura por calor, pH, etc.</li>
<li>Proteína perde função</li>
<li>Ex: clara do ovo cozida</li>
</ul>

<p>
<b>Fontes:</b>
</p>
<ul class="org-ul">
<li>Carnes, ovos, leite, leguminosas (feijão, soja)</li>
</ul>
</div>
</div>

<div id="outline-container-org74b2904" class="outline-4">
<h4 id="org74b2904"><span class="section-number-4">3.4.7.</span> 4. Ácidos Nucleicos</h4>
<div class="outline-text-4" id="text-3-4-7">
<p>
<b>DNA (Ácido Desoxirribonucleico):</b>
</p>
<ul class="org-ul">
<li>Material genético</li>
<li>Dupla hélice</li>
<li>Bases: A, T, C, G</li>
<li>Açúcar: desoxirribose</li>
</ul>

<p>
<b>RNA (Ácido Ribonucleico):</b>
</p>
<ul class="org-ul">
<li>Síntese de proteínas</li>
<li>Fita simples</li>
<li>Bases: A, U, C, G</li>
<li>Açúcar: ribose</li>
</ul>

<p>
<b>Nucleotídeos:</b>
</p>
<ul class="org-ul">
<li>Unidades que formam DNA e RNA</li>
<li>Fosfato + açúcar + base nitrogenada</li>
</ul>

<p>
<b>ATP (Adenosina Trifosfato):</b>
</p>
<ul class="org-ul">
<li>“Moeda energética” da célula</li>
<li>Armazena e fornece energia</li>
<li>Usado em todas as atividades celulares</li>
</ul>
</div>
</div>

