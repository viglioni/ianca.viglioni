<div id="outline-container-org340dbe4" class="outline-4">
<h4 id="org340dbe4"><span class="section-number-4">3.4.2.</span> Substâncias Inorgânicas</h4>
<div class="outline-text-4" id="text-3-4-2">
<p>
<b>1. Água (H₂O)</b>
</p>

<p>
<b>Substância mais abundante nos seres vivos:</b>
</p>
<ul class="org-ul">
<li>70-85% da massa corporal</li>
<li>Solvente universal</li>
<li>Meio para reações químicas</li>
</ul>

<p>
<b>Propriedades:</b>
</p>

<p>
<b>Polaridade:</b>
</p>
<ul class="org-ul">
<li>Molécula polar (δ+ no H, δ- no O)</li>
<li>Dissolve substâncias polares e iônicas</li>
<li>“Semelhante dissolve semelhante”</li>
</ul>

<p>
<b>Alto calor específico:</b>
</p>
<ul class="org-ul">
<li>Regula temperatura corporal</li>
<li>Evita variações bruscas de temperatura</li>
</ul>

<p>
<b>Coesão e adesão:</b>
</p>
<ul class="org-ul">
<li>Coesão: moléculas de água se atraem</li>
<li>Adesão: água adere a outras superfícies</li>
<li>Permite transporte de seiva nas plantas</li>
</ul>

<p>
<b>Funções da água:</b>
</p>
<ul class="org-ul">
<li><b>Solvente:</b> dissolve nutrientes, gases, íons</li>
<li><b>Transporte:</b> sangue, seiva, linfa</li>
<li><b>Regulação térmica:</b> suor, evaporação</li>
<li><b>Reações químicas:</b> hidrólise, fotossíntese</li>
<li><b>Lubrificação:</b> articulações, olhos</li>
</ul>

<p>
<b>2. Sais Minerais</b>
</p>

<p>
<b>Funções:</b>
</p>
<ul class="org-ul">
<li>Componentes estruturais (ossos, dentes)</li>
<li>Equilíbrio osmótico</li>
<li>Cofatores enzimáticos</li>
<li>Transmissão de impulsos nervosos</li>
</ul>

<p>
<b>Principais sais:</b>
</p>

<p>
<b>Cálcio (Ca):</b>
</p>
<ul class="org-ul">
<li>Ossos e dentes</li>
<li>Contração muscular</li>
<li>Coagulação sanguínea</li>
<li>Fontes: leite, queijo, vegetais verdes</li>
</ul>

<p>
<b>Ferro (Fe):</b>
</p>
<ul class="org-ul">
<li>Hemoglobina (transporte de O₂)</li>
<li>Mioglobina (músculos)</li>
<li>Deficiência → anemia</li>
<li>Fontes: carne, feijão, vegetais verdes</li>
</ul>

<p>
<b>Fósforo (P):</b>
</p>
<ul class="org-ul">
<li>Ossos e dentes</li>
<li>ATP (energia)</li>
<li>DNA e RNA</li>
<li>Fontes: leite, carnes, ovos</li>
</ul>

<p>
<b>Sódio (Na) e Potássio (K):</b>
</p>
<ul class="org-ul">
<li>Impulso nervoso</li>
<li>Equilíbrio osmótico</li>
<li>Contração muscular</li>
<li>Fontes: sal, frutas, legumes</li>
</ul>

<p>
<b>Iodo (I):</b>
</p>
<ul class="org-ul">
