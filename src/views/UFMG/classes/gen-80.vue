</pre>

<p>
<b>Desigualdade social:</b>
</p>

<pre class="example" id="org4ca5414">
Distribuição desigual de:
- Renda
- Educação
- Saúde
- Oportunidades
- Poder
</pre>

<p>
<b>Mobilidade social:</b>
</p>

<pre class="example" id="org794a510">
