<div id="outline-container-org59749df" class="outline-4">
<h4 id="org59749df"><span class="section-number-4">2.5.5.</span> Feudalismo (Idade Média</h4>
<div class="outline-text-4" id="text-2-5-5">
<ul class="org-ul">
<li>séculos V-XV)</li>
</ul>
<p>
<b>Feudalismo:</b> sistema político, econômico e social da Idade Média europeia
</p>

<p>
<b>Características:</b>
</p>

<p>
<b>1. Economia:</b>
</p>
<ul class="org-ul">
<li><b>Agricultura de subsistência</b> (produz para consumo próprio)</li>
<li>Pouco comércio</li>
<li><b>Manorialismo:</b> organização em <b>feudos</b> (propriedades rurais autossuficientes)</li>
</ul>

<p>
<b>2. Sociedade:</b>
</p>
<ul class="org-ul">
<li><b>Estamental:</b> sem mobilidade social</li>
<li>Três ordens (estados):</li>
<li><b>Clero (oratores):</b> rezam, Igreja</li>
<li><b>Nobreza (bellatores):</b> guerreiros, senhores feudais</li>
<li><b>Servos (laboratores):</b> trabalham, maioria da população</li>
</ul>

<p>
<b>3. Política:</b>
</p>
<ul class="org-ul">
<li><b>Descentralização:</b> poder fragmentado entre senhores feudais</li>
<li>Rei fraco</li>
<li><b>Relações de suserania e vassalagem:</b></li>
<li><b>Suserano:</b> concede feudo</li>
<li><b>Vassalo:</b> recebe feudo e jura fidelidade</li>
</ul>
</div>
</div>

<div id="outline-container-org34d04f9" class="outline-4">
<h4 id="org34d04f9"><span class="section-number-4">2.5.6.</span> O Feudo</h4>
<div class="outline-text-4" id="text-2-5-6">
<p>
<b>Feudo:</b> unidade produtiva básica
</p>

<p>
<b>Partes:</b>
</p>
<ul class="org-ul">
<li><b>Manso senhorial:</b> terras do senhor (castelo, melhores terras)</li>
<li><b>Manso servil:</b> terras dos servos (lotes individuais)</li>
<li><b>Manso comunal:</b> terras comuns (pastos, bosques)</li>
</ul>

<p>
<b>Obrigações servis:</b>
</p>
<ul class="org-ul">
<li><b>Corveia:</b> trabalho gratuito nas terras do senhor (3 dias/semana)</li>
<li><b>Talha:</b> parte da produção para o senhor (geralmente metade)</li>
<li><b>Banalidades:</b> taxas pelo uso de moinho, forno, etc.</li>
<li><b>Capitação:</b> imposto por indivíduo</li>
<li><b>Mão morta:</b> taxa para herdar terra</li>
</ul>

<p>
<b>Servo ≠ Escravo:</b>
</p>
<ul class="org-ul">
<li>Servo está <b>preso à terra</b> (não pode sair)</li>
<li>Mas não é propriedade do senhor</li>
<li>Tem direito ao lote de terra</li>
</ul>
</div>
</div>

<div id="outline-container-orgdc0d1f4" class="outline-4">
<h4 id="orgdc0d1f4"><span class="section-number-4">2.5.7.</span> Igreja Medieval</h4>
<div class="outline-text-4" id="text-2-5-7">
<p>
<b>Poder da Igreja:</b>
</p>
<ul class="org-ul">
<li>Único elo de unidade na Europa fragmentada</li>
<li>Controla educação</li>
<li>Grande proprietária de terras</li>
<li>Influência política</li>
</ul>

<p>
<b>Clero:</b>
</p>
<ul class="org-ul">
<li><b>Secular:</b> vive no “século” (mundo)</li>
<li>padres, bispos, papa</li>
<li><b>Regular:</b> segue regra (ordem religiosa)</li>
<li>monges em mosteiros</li>
</ul>

<p>
<b>Práticas:</b>
</p>
<ul class="org-ul">
<li><b>Dízimo:</b> 10% da produção para Igreja</li>
<li><b>Excomunhão:</b> expulsão da Igreja (grave!)</li>
<li><b>Indulgências:</b> perdão de pecados (vendidas)</li>
</ul>
</div>
</div>

<div id="outline-container-orgc2b4880" class="outline-4">
<h4 id="orgc2b4880"><span class="section-number-4">2.5.8.</span> Império Carolíngio (séculos VIII-IX)</h4>
<div class="outline-text-4" id="text-2-5-8">
<p>
<b>Francos:</b> povo germânico que domina Gália
</p>

<p>
<b>Carlos Magno (768-814):</b>
</p>
<ul class="org-ul">
<li>Rei dos francos</li>
<li>Expande território (França, Alemanha, Itália)</li>
<li><b>800 d.C.:</b> coroado <b>Imperador</b> pelo Papa</li>
<li>Tenta restaurar unidade do antigo Império Romano do Ocidente</li>
<li>Incentiva educação (Renascimento Carolíngio)</li>
</ul>

<p>
<b>Tratado de Verdun (843):</b>
</p>
<ul class="org-ul">
<li>Após morte de Carlos Magno</li>
<li>Império dividido entre 3 netos:</li>
<li><b>Carlos, o Calvo:</b> França Ocidental (origem da França)</li>
<li><b>Luís, o Germânico:</b> França Oriental (origem da Alemanha)</li>
<li><b>Lotário:</b> faixa central (Lotaríngia)</li>
</ul>
</div>
</div>

<div id="outline-container-org0635931" class="outline-4">
<h4 id="org0635931"><span class="section-number-4">2.5.9.</span> Alta Idade Média × Baixa Idade Média</h4>
<div class="outline-text-4" id="text-2-5-9">
<p>
<b>Alta Idade Média (séc. V-X):</b>
</p>
<ul class="org-ul">
<li>Formação do feudalismo</li>
<li>Invasões (vikings, muçulmanos, húngaros)</li>
<li>Economia fechada</li>
<li>Descentralização política</li>
<li>“Idade das Trevas” (declínio urbano e cultural)</li>
</ul>

<p>
<b>Baixa Idade Média (séc. XI-XV):</b>
</p>
<ul class="org-ul">
<li>Crescimento populacional</li>
<li>Expansão agrícola (inovações: arado pesado, rotação trienal)</li>
<li><b>Renascimento comercial e urbano</b></li>
<li><b>Cruzadas</b> (1095-1291)</li>
<li>Crise do feudalismo</li>
<li>Peste Negra (1347-1353)</li>
<li>mata 1/3 da Europa</li>
</ul>
</div>
</div>

<div id="outline-container-orga9a57f2" class="outline-4">
<h4 id="orga9a57f2"><span class="section-number-4">2.5.10.</span> Arte Medieval</h4>
<div class="outline-text-4" id="text-2-5-10">
<p>
<b>Arte Românica (séc. XI-XII):</b>
</p>
<ul class="org-ul">
<li>Arquitetura: igrejas pesadas, paredes grossas, pequenas janelas</li>
<li>Arcos de volta perfeita (semicirculares)</li>
<li>Interior escuro</li>
</ul>

<p>
<b>Arte Gótica (séc. XII-XV):</b>
</p>
<ul class="org-ul">
<li>Arquitetura: catedrais altas, grandes vitrais coloridos</li>
<li>Arcos ogivais (pontiagudos)</li>
<li>Arquitetura verticalizada (aponta para o céu)</li>
<li>Luz interior</li>
<li>Exemplo: Catedral de Notre-Dame (Paris)</li>
</ul>
</div>
</div>

