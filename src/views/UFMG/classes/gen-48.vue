<h4 id="org22c2363"><span class="section-number-4">2.5.1.</span> Crise do Império Romano e Antiguidade Tardia (séculos III-V d.C.)</h4>
<div class="outline-text-4" id="text-2-5-1">
<p>
<b>Antiguidade Tardia:</b> período de transição entre o mundo antigo e medieval (séculos III-VI d.C.)
</p>

<p>
<b>Crise do Século III:</b>
</p>

<p>
<b>Problemas políticos:</b>
</p>
<ul class="org-ul">
<li><b>Anarquia militar:</b> generais disputam poder</li>
<li>Imperadores fracos e de curta duração (26 imperadores em 50 anos!)</li>
<li>Instabilidade política</li>
</ul>

<p>
<b>Problemas econômicos:</b>
</p>
<ul class="org-ul">
<li>Crise agrícola e inflação</li>
<li>Diminuição do comércio</li>
<li>Crise do escravismo (sem novas conquistas → falta de escravos)</li>
</ul>

<p>
<b>Problemas sociais:</b>
</p>
<ul class="org-ul">
<li>Êxodo urbano (pessoas fogem para o campo)</li>
<li>Formação de <b>vilas (villae):</b> propriedades rurais autossuficientes (origem do feudalismo!)</li>
</ul>

<p>
