<div id="outline-container-org24d6bcb" class="outline-4">
<h4 id="org24d6bcb"><span class="section-number-4">2.5.2.</span> Divisão do Império Romano (395 d.C.)</h4>
<div class="outline-text-4" id="text-2-5-2">
<p>
<b>Teodósio</b> divide o império entre seus dois filhos:
</p>

<p>
<b>Império Romano do Ocidente:</b>
</p>
<ul class="org-ul">
<li>Capital: <b>Roma</b> (depois Ravena)</li>
<li>Mais vulnerável às invasões</li>
<li><b>Cai em 476 d.C.</b> (Odoacro depõe Rômulo Augusto)</li>
<li>Marca o fim da Antiguidade e início da Idade Média</li>
</ul>

<p>
<b>Império Romano do Oriental (Bizantino):</b>
</p>
<ul class="org-ul">
<li>Capital: <b>Constantinopla</b> (antiga Bizâncio)</li>
<li>Mais rico e urbanizado</li>
<li>Sobrevive até <b>1453</b> (queda para os turcos otomanos)</li>
</ul>
</div>
</div>

<div id="outline-container-orgc4f2cfa" class="outline-4">
<h4 id="orgc4f2cfa"><span class="section-number-4">2.5.3.</span> Invasões Bárbaras (séculos IV-V)</h4>
<div class="outline-text-4" id="text-2-5-3">
<p>
<b>Bárbaros:</b> povos germânicos fora das fronteiras romanas
</p>

<p>
<b>Principais povos:</b>
</p>
<ul class="org-ul">
<li><b>Francos:</b> Gália (França)</li>
<li><b>Visigodos:</b> Península Ibérica (Espanha/Portugal)</li>
<li><b>Ostrogodos:</b> Itália</li>
<li><b>Vândalos:</b> Norte da África</li>
<li><b>Anglos e Saxões:</b> Britânia (Inglaterra)</li>
<li><b>Hunos:</b> (não germânicos) vindos da Ásia</li>
<li>Átila</li>
</ul>

<p>
<b>Consequências:</b>
</p>
<ul class="org-ul">
<li><b>Fim do Império Romano do Ocidente (476 d.C.)</b></li>
<li>Formação dos <b>reinos germânicos</b></li>
<li>Ruralização da Europa</li>
<li>Fusão da cultura romana com germânica</li>
</ul>
</div>
</div>

<div id="outline-container-org709abab" class="outline-4">
<h4 id="org709abab"><span class="section-number-4">2.5.4.</span> Cristianismo</h4>
<div class="outline-text-4" id="text-2-5-4">
<p>
<b>Origem:</b>
</p>
<ul class="org-ul">
<li>Século I d.C., na Judeia (província romana)</li>
<li>Fundador: <b>Jesus Cristo</b></li>
<li>Apóstolos espalham a mensagem</li>
</ul>

<p>
<b>Perseguições:</b>
</p>
<ul class="org-ul">
<li>Romanos perseguem cristãos (culto ao imperador era obrigatório)</li>
<li>Exemplos: Nero, Diocleciano</li>
</ul>

<p>
<b>Legalização:</b>
</p>
<ul class="org-ul">
<li>*313 d.C.</li>
<li>Édito de Milão* (Constantino): liberdade religiosa</li>
<li>*380 d.C.</li>
<li>Édito de Tessalônica* (Teodósio): cristianismo como <b>religião oficial</b></li>
</ul>

<p>
<b>Papel na Idade Média:</b>
</p>
<ul class="org-ul">
<li>Igreja única instituição organizada após queda de Roma</li>
<li>Preserva cultura clássica (mosteiros copiam livros)</li>
<li>Grande poder político e econômico</li>
</ul>
</div>
