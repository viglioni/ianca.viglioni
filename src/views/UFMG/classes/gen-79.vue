<div id="outline-container-org1a26ba5" class="outline-4">
<h4 id="org1a26ba5"><span class="section-number-4">4.5.6.</span> PARTE 5: Lógica Básica</h4>
<div class="outline-text-4" id="text-4-5-6">
<p>
<b>Proposição:</b>
</p>

<pre class="example" id="org198da1f">
Afirmação que pode ser verdadeira ou falsa

Ex: "Sócrates é mortal" (V)
     "2 + 2 = 5" (F)
</pre>

<p>
<b>Silogismo (Aristóteles):</b>
</p>

<pre class="example" id="orgafd7216">
Premissa maior: Todo homem é mortal
Premissa menor: Sócrates é homem
Conclusão: Logo, Sócrates é mortal
</pre>

<p>
<b>Princípios lógicos:</b>
</p>

<pre class="example" id="orgc733e43">
Identidade: A é A
Não-contradição: A não pode ser A e não-A ao mesmo tempo
Terceiro excluído: ou A ou não-A (não há terceira opção)
</pre>

<p>
<b>Falácias (erros de raciocínio):</b>
</p>

<pre class="example" id="org5c924cc">
Ad hominem: atacar a pessoa, não o argumento
Apelo à autoridade: "X disse, então é verdade"
Falsa analogia: comparações inadequadas
Post hoc: "depois disso, logo por causa disso"
</pre>
</div>
