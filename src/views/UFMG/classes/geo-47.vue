<h4 id="orgab2317d"><span class="section-number-4">2.4.1.</span> Estrutura Interna da Terra</h4>
<div class="outline-text-4" id="text-2-4-1">
<p>
<b>Camadas da Terra (do centro para fora):</b>
</p>

<p>
<b>1. Núcleo Interno</b>
</p>
<ul class="org-ul">
<li>Estado: <b>sólido</b></li>
<li>Composição: ferro e níquel</li>
<li>Temperatura: ~5.000°C</li>
<li>Espessura: ~1.200 km</li>
</ul>

<p>
<b>2. Núcleo Externo</b>
</p>
<ul class="org-ul">
<li>Estado: <b>líquido</b></li>
<li>Composição: ferro e níquel fundidos</li>
<li>Responsável pelo campo magnético terrestre</li>
<li>Espessura: ~2.300 km</li>
</ul>

<p>
<b>3. Manto</b>
</p>
<ul class="org-ul">
<li>Estado: <b>pastoso/viscoso</b> (magma)</li>
<li>Composição: silicatos de ferro e magnésio</li>
<li>Movimento das correntes de convecção (movem placas tectônicas)</li>
<li>Espessura: ~2.900 km (maior camada!)</li>
</ul>

<p>
<b>4. Crosta (Litosfera)</b>
</p>
<ul class="org-ul">
<li>Estado: <b>sólido</b></li>
<li>Camada mais fina e externa</li>
<li>Onde vivemos</li>
<li>Tipos:</li>
<li><b>Crosta oceânica:</b> mais densa, mais fina (5-10 km)</li>
<li><b>Crosta continental:</b> menos densa, mais espessa (30-70 km)</li>
</ul>
</div>
</div>

<div id="outline-container-org4bc78f5" class="outline-4">
<h4 id="org4bc78f5"><span class="section-number-4">2.4.2.</span> Placas Tectônicas</h4>
<div class="outline-text-4" id="text-2-4-2">
<p>
<b>Teoria da Deriva Continental (Alfred Wegener, 1912):</b>
</p>
<ul class="org-ul">
<li>Os continentes já formaram um supercontinente: <b>Pangeia</b></li>
<li>Evidências: fósseis iguais em continentes distantes, encaixe da África com América do Sul</li>
</ul>

<p>
<b>Tectônica de Placas:</b>
</p>
<ul class="org-ul">
<li>A litosfera é dividida em <b>placas tectônicas</b></li>
<li>Placas flutuam sobre o manto</li>
<li>Movimento causado por <b>correntes de convecção</b> no manto</li>
</ul>

<p>
<b>Principais placas:</b>
</p>
<ul class="org-ul">
<li>Placa Sul-Americana</li>
<li>Placa de Nazca</li>
<li>Placa Africana</li>
<li>Placa Euroasiática</li>
<li>Placa do Pacífico</li>
<li>Placa Norte-Americana</li>
</ul>
</div>
</div>

<div id="outline-container-org74c5d69" class="outline-4">
<h4 id="org74c5d69"><span class="section-number-4">2.4.3.</span> Tipos de Limites entre Placas</h4>
<div class="outline-text-4" id="text-2-4-3">
<p>
<b>1. Limites Divergentes (Construtivos)</b>
</p>
<ul class="org-ul">
<li>Placas se <b>afastam</b></li>
<li>Formam <b>dorsais oceânicas</b> (cadeias de montanhas submarinas)</li>
<li>Novo magma sobe e cria nova crosta</li>
<li>Exemplo: Dorsal Meso-Atlântica (separa América da África/Europa)</li>
</ul>

<p>
<b>2. Limites Convergentes (Destrutivos)</b>
</p>
<ul class="org-ul">
<li>Placas se <b>aproximam</b></li>
<li>Três tipos:</li>
</ul>

<p>
<b>a) Oceânica + Oceânica:</b>
</p>
<ul class="org-ul">
<li>Forma <b>fossas oceânicas</b> e <b>arcos de ilhas vulcânicas</b></li>
<li>Exemplo: Fossa das Marianas (11 km de profundidade!)</li>
</ul>

<p>
<b>b) Oceânica + Continental:</b>
</p>
<ul class="org-ul">
<li>Placa oceânica <b>subducta</b> (mergulha) sob a continental</li>
<li>Forma <b>cadeias montanhosas</b> e <b>vulcões</b></li>
<li>Exemplo: Cordilheira dos Andes (Nazca + Sul-Americana)</li>
</ul>

<p>
<b>c) Continental + Continental:</b>
</p>
<ul class="org-ul">
<li>Nenhuma subduz (mesma densidade)</li>
<li>Forma <b>cadeias montanhosas gigantes</b></li>
<li>Exemplo: Himalaia (Índia + Eurásia)</li>
<li>Monte Everest!</li>
</ul>

<p>
<b>3. Limites Transformantes (Conservativos)</b>
</p>
<ul class="org-ul">
<li>Placas <b>deslizam lateralmente</b></li>
<li>Não criam nem destroem crosta</li>
<li>Causam <b>terremotos</b></li>
<li>Exemplo: Falha de San Andreas (Califórnia, EUA)</li>
</ul>
</div>
</div>

<div id="outline-container-org9a71352" class="outline-4">
<h4 id="org9a71352"><span class="section-number-4">2.4.4.</span> Atividades Vulcânicas e Sísmicas</h4>
<div class="outline-text-4" id="text-2-4-4">
<p>
<b>Vulcanismo:</b>
</p>
<ul class="org-ul">
<li>Ocorre principalmente em:</li>
<li>Limites convergentes (subducção)</li>
<li>Limites divergentes (dorsais)</li>
<li>Pontos quentes (hot spots)</li>
<li>Havaí</li>
</ul>

<p>
<b>Círculo de Fogo do Pacífico:</b>
</p>
<ul class="org-ul">
<li>Região com maior atividade sísmica e vulcânica</li>
<li>Contorna o Oceano Pacífico</li>
<li>90% dos terremotos mundiais</li>
</ul>

<p>
<b>Terremotos (Abalos Sísmicos):</b>
</p>
<ul class="org-ul">
<li>Causados por movimento brusco de placas</li>
<li><b>Hipocentro (foco):</b> ponto onde se origina</li>
<li><b>Epicentro:</b> ponto na superfície acima do hipocentro</li>
<li><b>Escala Richter:</b> mede magnitude (energia liberada)</li>
<li><b>Escala Mercalli:</b> mede intensidade (danos causados)</li>
</ul>

<p>
<b>Brasil e Terremotos:</b>
</p>
<ul class="org-ul">
<li>Brasil está no <b>centro da Placa Sul-Americana</b></li>
<li>Longe de bordas de placas</li>
<li>Poucos e fracos terremotos</li>
</ul>
</div>
</div>

<div id="outline-container-orgd90673b" class="outline-4">
<h4 id="orgd90673b"><span class="section-number-4">2.4.5.</span> Tipos de Rochas</h4>
<div class="outline-text-4" id="text-2-4-5">
<p>
<b>1. Rochas Magmáticas (Ígneas)</b>
</p>
<ul class="org-ul">
<li>Formadas pela <b>solidificação do magma</b></li>
<li>Tipos:</li>
<li><b>Intrusivas (plutônicas):</b> magma esfria lentamente no interior</li>
<li>cristais grandes</li>
<li>Exemplo: granito</li>
<li><b>Extrusivas (vulcânicas):</b> magma esfria rapidamente na superfície</li>
<li>cristais pequenos</li>
<li>Exemplo: basalto</li>
</ul>

<p>
<b>2. Rochas Sedimentares</b>
</p>
<ul class="org-ul">
<li>Formadas por <b>acúmulo e compactação de sedimentos</b></li>
<li>Processo: intemperismo → erosão → transporte → deposição → compactação</li>
<li>Podem conter <b>fósseis</b></li>
<li>Exemplos: arenito, calcário, argila</li>
</ul>

<p>
<b>3. Rochas Metamórficas</b>
</p>
<ul class="org-ul">
<li>Formadas pela <b>transformação</b> de outras rochas (calor e pressão)</li>
<li>Não derretem</li>
<li>Exemplos:</li>
<li>Mármore (de calcário)</li>
<li>Gnaisse (de granito)</li>
<li>Ardósia (de argila)</li>
</ul>

<p>
<b>Ciclo das Rochas:</b>
</p>

<pre class="example" id="org2a37496">
Magmática → (intemperismo) → Sedimentar
    ↑                              ↓
    ←── (metamorfismo) ←── Metamórfica
</pre>
</div>
</div>

<div id="outline-container-orgc8869c5" class="outline-4">
<h4 id="orgc8869c5"><span class="section-number-4">2.4.6.</span> Formas de Relevo</h4>
<div class="outline-text-4" id="text-2-4-6">
<p>
<b>Relevo:</b> conjunto de formas da superfície terrestre
</p>

<p>
<b>Principais formas:</b>
</p>

<p>
<b>1. Planícies</b>
</p>
<ul class="org-ul">
<li>Terrenos <b>planos e baixos</b> (altitude &lt; 300m geralmente)</li>
<li>Formadas por deposição de sedimentos</li>
<li>Exemplo: Planície Amazônica, Planície do Pantanal</li>
</ul>

<p>
<b>2. Planaltos</b>
</p>
<ul class="org-ul">
<li>Terrenos <b>elevados e planos no topo</b></li>
<li>Sofrem erosão</li>
<li>Altitude &gt; 300m</li>
<li>Exemplo: Planalto Central, Planalto da Borborema</li>
</ul>

<p>
<b>3. Montanhas</b>
</p>
<ul class="org-ul">
<li>Terrenos muito <b>elevados</b> com <b>topos pontiagudos</b></li>
<li>Formadas por tectonismo (dobramento/falha)</li>
<li>Exemplo: Cordilheira dos Andes, Himalaia, Alpes</li>
</ul>

<p>
<b>4. Depressões</b>
</p>
<ul class="org-ul">
<li>Terrenos <b>rebaixados</b></li>
<li>Podem estar abaixo ou acima do nível do mar</li>
<li>Exemplo: Depressão Sertaneja</li>
</ul>

<p>
<b>5. Vales</b>
</p>
<ul class="org-ul">
<li>Áreas <b>baixas entre montanhas</b></li>
<li>Formados por erosão (rios)</li>
</ul>
</div>
</div>

<div id="outline-container-org00aaa70" class="outline-4">
<h4 id="org00aaa70"><span class="section-number-4">2.4.7.</span> Agentes Modeladores do Relevo</h4>
<div class="outline-text-4" id="text-2-4-7">
<p>
<b>Agentes Internos (Endógenos):</b>
</p>
<ul class="org-ul">
<li><b>Tectonismo:</b> movimento de placas (dobramento, falhas)</li>
<li><b>Vulcanismo:</b> erupções vulcânicas</li>
<li><b>Abalos sísmicos:</b> terremotos</li>
</ul>

<p>
<b>Agentes Externos (Exógenos):</b>
</p>
<ul class="org-ul">
<li><b>Intemperismo:</b> decomposição de rochas (físico, químico, biológico)</li>
<li><b>Erosão:</b> desgaste e transporte de material</li>
<li>Erosão fluvial (rios)</li>
<li>Erosão eólica (ventos)</li>
<li>Erosão glacial (geleiras)</li>
<li>Erosão marinha (ondas)</li>
<li><b>Ação humana:</b> desmatamento, mineração, urbanização</li>
</ul>
</div>
</div>

<div id="outline-container-org984a103" class="outline-4">
<h4 id="org984a103"><span class="section-number-4">2.4.8.</span> Mineração no Brasil</h4>
<div class="outline-text-4" id="text-2-4-8">
<p>
<b>Principais minérios:</b>
</p>

<p>
<b>Ferro:</b>
</p>
<ul class="org-ul">
<li>Maior produtor: Minas Gerais (Quadrilátero Ferrífero)</li>
<li>Pará (Serra dos Carajás)</li>
<li>Uso: siderurgia (aço)</li>
</ul>

<p>
<b>Bauxita (Alumínio):</b>
</p>
<ul class="org-ul">
<li>Pará (maior produtor)</li>
<li>Uso: indústria de alumínio</li>
</ul>

<p>
<b>Ouro:</b>
</p>
<ul class="org-ul">
<li>Minas Gerais, Pará</li>
<li>Histórico: Ciclo do Ouro (século XVIII)</li>
</ul>

<p>
<b>Manganês:</b>
</p>
<ul class="org-ul">
<li>Pará, Minas Gerais</li>
<li>Uso: ligas metálicas</li>
</ul>

<p>
<b>Nióbio:</b>
</p>
<ul class="org-ul">
<li>Brasil detém ~98% das reservas mundiais!</li>
<li>Minas Gerais, Goiás</li>
<li>Uso: ligas de aço especiais</li>
</ul>

<p>
<b>Impactos da Mineração:</b>
</p>

<p>
<b>Positivos:</b>
</p>
<ul class="org-ul">
<li>Geração de empregos</li>
<li>Desenvolvimento econômico</li>
<li>Exportações</li>
</ul>

<p>
<b>Negativos:</b>
</p>
<ul class="org-ul">
<li>Desmatamento</li>
<li>Poluição de rios e solo</li>
<li>Rompimento de barragens (Mariana 2015, Brumadinho 2019)</li>
<li>Conflitos com comunidades</li>
</ul>
</div>
