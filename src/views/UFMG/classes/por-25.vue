<li>Se você <b>estudar</b>, passará.</li>
</ul>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>quando eu cant*ar*</li>
<li>quando tu cant*ares*</li>
<li>quando ele cant*ar*</li>
<li>quando nós cant*armos*</li>
<li>quando vós cant*ardes*</li>
<li>quando eles cant*arem*</li>
</ul>

<p>
