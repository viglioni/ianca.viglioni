<div id="outline-container-org051268a" class="outline-4">
<h4 id="org051268a"><span class="section-number-4">5.5.6.</span> Modos Verbais</h4>
<div class="outline-text-4" id="text-5-5-6">
<p>
<b>Modo</b> indica a <b>atitude</b> do falante em relação ao que está dizendo.
</p>
</div>

<div id="outline-container-org83738a4" class="outline-5">
<h5 id="org83738a4"><span class="section-number-5">5.5.6.1.</span> 1. Modo Indicativo</h5>
<div class="outline-text-5" id="text-5-5-6-1">
<p>
Expressa <b>certeza, fato real</b> (afirmação, negação, pergunta).
</p>

<p>
<b>Uso:</b> ações concretas, situações reais.
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Eu <b>estudo</b> todos os dias. (certeza)</li>
<li>Ela <b>viajou</b> ontem. (fato)</li>
<li>Vocês <b>chegarão</b> cedo? (pergunta sobre fato)</li>
</ul>
</div>
</div>

<div id="outline-container-org6e0524e" class="outline-5">
<h5 id="org6e0524e"><span class="section-number-5">5.5.6.2.</span> 2. Modo Subjuntivo</h5>
<div class="outline-text-5" id="text-5-5-6-2">
<p>
Expressa <b>dúvida, possibilidade, desejo, hipótese</b>.
</p>

<p>
<b>Uso:</b> ações incertas, dependentes de condições.
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Espero que ele <b>estude</b>. (desejo)</li>
<li>Se eu <b>pudesse</b>, viajaria. (hipótese)</li>
<li>Talvez eles <b>venham</b>. (dúvida)</li>
</ul>

<p>
<b>Palavras indicadoras:</b> espero que, talvez, se, caso, quando (futuro)
</p>
</div>
</div>

<div id="outline-container-org6485942" class="outline-5">
<h5 id="org6485942"><span class="section-number-5">5.5.6.3.</span> 3. Modo Imperativo</h5>
<div class="outline-text-5" id="text-5-5-6-3">
<p>
Expressa <b>ordem, pedido, conselho</b>.
</p>

<p>
<b>Uso:</b> dar comandos, instruções.
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li><b>Estude</b> mais! (ordem)</li>
<li><b>Venha</b> aqui, por favor. (pedido)</li>
<li><b>Tenha</b> cuidado. (conselho)</li>
</ul>

<p>
<b>Formas:</b>
</p>
<ul class="org-ul">
<li><b>Imperativo afirmativo:</b> Faça isso!</li>
<li><b>Imperativo negativo:</b> Não faça isso!</li>
</ul>
</div>
</div>
</div>

