<li>nós cant*amos*</li>
<li>vós cant*astes*</li>
<li>eles cant*aram*</li>
</ul>
</div>
</div>

<div id="outline-container-org7634e81" class="outline-5">
<h5 id="org7634e81"><span class="section-number-5">5.5.7.3.</span> Pretérito Imperfeito</h5>
<div class="outline-text-5" id="text-5-5-7-3">
<p>
Ação <b>contínua, habitual ou inacabada</b> no passado.
</p>

<p>
<b>Usos:</b> 1. Ação habitual: Eu <b>estudava</b> todos os dias quando era criança. 2. Ação contínua: Ela <b>lia</b> quando eu cheguei. 3. Descrição: O dia <b>estava</b> lindo.
</p>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>eu cant*ava*</li>
<li>tu cant*avas*</li>
<li>ele cant*ava*</li>
<li>nós cant*ávamos*</li>
<li>vós cant*áveis*</li>
<li>eles cant*avam*</li>
</ul>

<p>
<b>Diferença Perfeito vs Imperfeito:</b>
</p>
<ul class="org-ul">
<li><b>Perfeito:</b> Eu <b>li</b> o livro. (ação concluída)</li>
<li><b>Imperfeito:</b> Eu <b>lia</b> o livro. (ação em andamento/habitual)</li>
</ul>
</div>
</div>

<div id="outline-container-org7f5910e" class="outline-5">
<h5 id="org7f5910e"><span class="section-number-5">5.5.7.4.</span> Pretérito Mais-que-Perfeito</h5>
<div class="outline-text-5" id="text-5-5-7-4">
<p>
Ação <b>anterior</b> a outra ação no passado.
</p>

<p>
<b>Uso:</b> passado do passado.
</p>

<p>
<b>Exemplo:</b> Quando cheguei, ele já <b>saíra</b>. (saiu antes de eu chegar)
</p>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>eu cant*ara*</li>
<li>tu cant*aras*</li>
<li>ele cant*ara*</li>
<li>nós cant*áramos*</li>
<li>vós cant*áreis*</li>
<li>eles cant*aram*</li>
</ul>

<p>
<b>Forma composta (mais usada):</b> tinha/havia + particípio
</p>
<ul class="org-ul">
<li>Quando cheguei, ele já <b>tinha saído</b>.</li>
</ul>
</div>
</div>

