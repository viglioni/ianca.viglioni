<div id="outline-container-org18c4cea" class="outline-5">
<h5 id="org18c4cea"><span class="section-number-5">5.5.10.5.</span> Exercício 5</h5>
<div class="outline-text-5" id="text-5-5-10-5">
<p>
Qual a diferença: a) Ele viajou. b) Ele viajava.
</p>

<p>
<b>Solução:</b>
</p>

<ol class="org-ol">
<li><b>Pretérito Perfeito:</b> ação concluída, pontual (“Ele viajou ontem.”)</li>

<li><b>Pretérito Imperfeito:</b> ação habitual ou contínua (“Ele viajava todos os meses.” ou “Ele viajava quando o vi.”)</li>
</ol>
</div>
</div>
