<li>Eles <b>viajarão</b> no mês que vem.</li>
</ul>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>eu cantar*ei*</li>
<li>tu cantar*ás*</li>
<li>ele cantar*á*</li>
<li>nós cantar*emos*</li>
<li>vós cantar*eis*</li>
<li>eles cantar*ão*</li>
</ul>
