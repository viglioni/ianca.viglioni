<div id="outline-container-orgf27a0d7" class="outline-5">
<h5 id="orgf27a0d7"><span class="section-number-5">5.5.7.6.</span> Futuro do Pretérito</h5>
<div class="outline-text-5" id="text-5-5-7-6">
<p>
Ação <b>futura em relação a um momento passado</b>; também expressa <b>condição</b>.
</p>

<p>
<b>Usos:</b> 1. Futuro do passado: Ele disse que <b>viria</b>. (viria = futuro em relação ao passado) 2. Condição: Se eu pudesse, <b>viajaria</b>. (condição) 3. Incerteza/dúvida: Será que ela <b>estaria</b> em casa? (dúvida educada)
</p>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>eu cantar*ia*</li>
<li>tu cantar*ias*</li>
<li>ele cantar*ia*</li>
