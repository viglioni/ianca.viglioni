<div id="outline-container-orgbdb90a9" class="outline-4">
<h4 id="orgbdb90a9"><span class="section-number-4">5.5.11.</span> Dicas para a Prova</h4>
<div class="outline-text-4" id="text-5-5-11">
<ol class="org-ol">
<li><b>Indicativo:</b> certeza, fatos reais</li>
<li><b>Subjuntivo:</b> dúvida, desejo, hipótese (que, se, talvez)</li>
<li><b>Imperativo:</b> ordem, pedido (sem EU)</li>
<li><b>Perfeito:</b> ação concluída (-ei, -ou, -aram)</li>
<li><b>Imperfeito:</b> ação contínua/habitual (-ava, -ia)</li>
<li><b>Futuro do Pretérito:</b> condição (-ia: faria, seria)</li>
<li><b>Subjuntivo Imperfeito:</b> sempre -sse (estudasse, fizesse)</li>
<li><b>Futuro do Subjuntivo:</b> quando, se futuro (estudar, fizer)</li>
</ol>
</div>
</div>

<div id="outline-container-org3775f5d" class="outline-4">
<h4 id="org3775f5d"><span class="section-number-4">5.5.12.</span> Conceitos-Chave para Memorizar</h4>
<div class="outline-text-4" id="text-5-5-12">
<p>
<b>Modos:</b>
</p>
<ul class="org-ul">
<li><b>Indicativo:</b> certeza</li>
<li><b>Subjuntivo:</b> dúvida, desejo</li>
<li><b>Imperativo:</b> ordem</li>
</ul>

<p>
<b>Pretéritos (Indicativo):</b>
</p>
<ul class="org-ul">
<li><b>Perfeito:</b> acabou (estudei)</li>
<li><b>Imperfeito:</b> contínuo/habitual (estudava)</li>
<li><b>Mais-que-Perfeito:</b> passado do passado (estudara/tinha estudado)</li>
</ul>

<p>
<b>Futuros (Indicativo):</b>
</p>
<ul class="org-ul">
<li><b>Futuro do Presente:</b> vai acontecer (estudarei)</li>
<li><b>Futuro do Pretérito:</b> condição (estudaria)</li>
</ul>

<p>
<b>Subjuntivo (palavras-chave):</b>
</p>
<ul class="org-ul">
<li><b>Presente:</b> que, talvez</li>
<li><b>Imperfeito:</b> se, caso</li>
<li><b>Futuro:</b> quando, se (futuro)</li>
</ul>
</div>
</div>

