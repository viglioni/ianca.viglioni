<h5 id="org195cb5f"><span class="section-number-5">5.5.7.1.</span> Presente do Indicativo</h5>
<div class="outline-text-5" id="text-5-5-7-1">
<p>
Ação que ocorre <b>no momento da fala</b> ou <b>habitualmente</b>.
</p>

<p>
<b>Usos:</b> 1. Momento presente: Eu <b>estudo</b> agora. 2. Ação habitual: Ela <b>acorda</b> cedo todos os dias. 3. Verdade universal: A Terra <b>gira</b> em torno do Sol. 4. Presente histórico: Em 1500, Cabral <b>chega</b> ao Brasil.
</p>

