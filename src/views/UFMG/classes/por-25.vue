<h5 id="orgf6f004c"><span class="section-number-5">5.5.7.2.</span> Pretérito Perfeito</h5>
<div class="outline-text-5" id="text-5-5-7-2">
<p>
Ação <b>concluída</b> no passado.
</p>

<p>
<b>Uso:</b> fato pontual, acabado.
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
