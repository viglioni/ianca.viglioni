<li>Eles <b>chegaram</b> às 8h.</li>
</ul>

<p>
*Conjugação
</p>
<ul class="org-ul">
<li>CANTAR:*</li>
<li>eu cant*ei*</li>
<li>tu cant*aste*</li>
