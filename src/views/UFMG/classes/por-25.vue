</p>

<p>
<b>Solução:</b>
</p>

<ol class="org-ol">
<li>estudava: Pretérito Imperfeito do Indicativo (ação habitual no passado)</li>

<li>venha: Presente do Subjuntivo (desejo: “espero que”)</li>

<li>pudesse: Pretérito Imperfeito do Subjuntivo (hipótese: “se”) viajaria: Futuro do Pretérito do Indicativo (condição)</li>
</ol>
</div>
</div>

