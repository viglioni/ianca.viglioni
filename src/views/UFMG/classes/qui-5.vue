<li>Indústria de alimentos (remover partículas metálicas)</li>
</ul>

<p>
<b>Equipamento:</b> Ímã ou eletroímã
</p>

<p>
<b>Vantagens:</b> Rápida e eficiente <b>Limitações:</b> Só funciona com materiais magnéticos
</p>
</div>
