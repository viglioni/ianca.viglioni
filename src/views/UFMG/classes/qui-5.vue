<div id="outline-container-org0fc11bd" class="outline-4">
<h4 id="org0fc11bd"><span class="section-number-4">1.5.11.</span> 8. Decantação</h4>
<div class="outline-text-4" id="text-1-5-11">
<p>
<b>Princípio:</b> Separar por diferença de densidade, deixando em repouso
</p>

<p>
<b>Quando usar:</b> Líquido + sólido insolúvel OU dois líquidos imiscíveis
</p>
</div>

<div id="outline-container-org924e99d" class="outline-5">
<h5 id="org924e99d"><span class="section-number-5">1.5.11.1.</span> Decantação Sólido-Líquido</h5>
<div class="outline-text-5" id="text-1-5-11-1">
<ul class="org-ul">
<li>Deixa-se em repouso</li>
<li>Sólido mais denso decanta (vai para o fundo)</li>
<li>Líquido sobrenadante é vertido ou sifonado</li>
</ul>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Tratamento de água (floculação + decantação)</li>
<li>Deixar água barrenta decantar</li>
</ul>
</div>
</div>

<div id="outline-container-org1e483f7" class="outline-5">
<h5 id="org1e483f7"><span class="section-number-5">1.5.11.2.</span> Decantação Líquido-Líquido</h5>
<div class="outline-text-5" id="text-1-5-11-2">
<ul class="org-ul">
<li>Usa <b>funil de separação (funil de bromo)</b></li>
<li>Líquidos imiscíveis (que não se misturam)</li>
<li>Líquido mais denso fica embaixo</li>
</ul>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar água e óleo</li>
<li>Separar água e gasolina</li>
<li>Extração de petróleo</li>
</ul>

<p>
<b>Processo com funil de separação:</b> 1. Coloca-se a mistura no funil 2. Aguarda-se a separação das fases 3. Abre-se a torneira e coleta-se o líquido de baixo 4. Fecha-se a torneira quando chegar ao líquido de cima 5. Retira-se o líquido de cima pela parte superior
</p>
</div>
</div>
</div>

<div id="outline-container-org192a6a8" class="outline-4">
<h4 id="org192a6a8"><span class="section-number-4">1.5.12.</span> 9. Centrifugação</h4>
<div class="outline-text-4" id="text-1-5-12">
<p>
<b>Princípio:</b> Acelerar a decantação por rotação em alta velocidade
</p>

<p>
<b>Quando usar:</b> Quando a decantação natural é muito lenta
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar sangue (células + plasma)</li>
<li>Centrifugar roupa em máquina de lavar</li>
<li>Análises clínicas (separar componentes do sangue, urina)</li>
<li>Separar creme de leite</li>
</ul>

<p>
<b>Equipamento:</b> Centrífuga
</p>

<p>
<b>Vantagens:</b> Muito mais rápida que decantação simples <b>Aplicação:</b> Essencial em laboratórios médicos e de pesquisa
</p>
</div>
</div>

<div id="outline-container-orgb5e6c18" class="outline-4">
<h4 id="orgb5e6c18"><span class="section-number-4">1.5.13.</span> Processos de Separação de Misturas Homogêneas</h4>
</div>
<div id="outline-container-orgbed4455" class="outline-4">
<h4 id="orgbed4455"><span class="section-number-4">1.5.14.</span> 10. Evaporação</h4>
<div class="outline-text-4" id="text-1-5-14">
<p>
<b>Princípio:</b> Evaporar completamente o solvente, deixando o soluto
</p>

<p>
<b>Quando usar:</b> Sólido dissolvido + líquido volátil (quando não precisamos do líquido)
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Obter sal marinho (salinas)</li>
<li>Evaporar água para obter sal de cozinha</li>
<li>Secagem natural de roupas</li>
</ul>

<p>
<b>Processo:</b>
</p>
<ul class="org-ul">
<li>Pode ser natural (sol, vento)</li>
<li>Ou por aquecimento</li>
</ul>

<p>
<b>Desvantagem:</b> Perde-se o líquido
</p>
</div>
</div>

<div id="outline-container-org0d77f19" class="outline-4">
<h4 id="org0d77f19"><span class="section-number-4">1.5.15.</span> 11. Destilação Simples</h4>
<div class="outline-text-4" id="text-1-5-15">
<p>
<b>Princípio:</b> Separar por diferença de temperatura de ebulição, com recuperação do líquido
</p>

<p>
<b>Quando usar:</b> Sólido dissolvido + líquido OU líquidos com diferença grande de TE
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Destilar água (obter água destilada)</li>
<li>Separar água de sal dissolvido</li>
<li>Produzir água potável do mar</li>
</ul>

<p>
<b>Equipamento:</b>
</p>
<ul class="org-ul">
<li>Balão de destilação</li>
<li>Condensador (serpentina resfriada)</li>
<li>Termômetro</li>
<li>Recipiente coletor</li>
</ul>

<p>
<b>Processo:</b> 1. Aquece-se a mistura no balão 2. Líquido com menor TE evapora primeiro 3. Vapor sobe e entra no condensador 4. Condensador resfria o vapor, que volta ao estado líquido 5. Líquido destilado é coletado 6. Sólido (ou líquido de maior TE) fica no balão
</p>

<p>
<b>Resultados:</b>
</p>
<ul class="org-ul">
<li><b>Destilado:</b> Líquido que foi vaporizado e condensado (purificado)</li>
<li><b>Resíduo:</b> Material que ficou no balão</li>
</ul>
</div>
</div>

<div id="outline-container-org000c2b2" class="outline-4">
<h4 id="org000c2b2"><span class="section-number-4">1.5.16.</span> 12. Destilação Fracionada</h4>
<div class="outline-text-4" id="text-1-5-16">
<p>
<b>Princípio:</b> Separar líquidos com temperaturas de ebulição próximas
</p>

<p>
<b>Quando usar:</b> Mistura de líquidos miscíveis com TEs diferentes mas próximas
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar componentes do petróleo (gasolina, querosene, diesel, etc.)</li>
<li>Separar álcool de água (produção de cachaça, whisky)</li>
<li>Separar componentes do ar liquefeito (O₂, N₂, gases nobres)</li>
<li>Produção de bebidas destiladas</li>
</ul>

<p>
<b>Equipamento:</b> Similar à destilação simples, mas com <b>coluna de fracionamento</b>
</p>

<p>
<b>Coluna de fracionamento:</b>
</p>
<ul class="org-ul">
<li>Torre com obstáculos ou pratos</li>
<li>Permite múltiplas evaporações e condensações</li>
<li>Aumenta a eficiência da separação</li>
</ul>

<p>
<b>Processo (petróleo):</b>
</p>

<pre class="example" id="orgcca4b82">
 Topo (mais frio)
     ↑
Gás (GLP)
     ↑
Gasolina (~100-150°C)
     ↑
Querosene (~150-250°C)
     ↑
Diesel (~250-350°C)
     ↑
Óleos lubrificantes
     ↑
Asfalto (resíduo)

Base (mais quente)
</pre>

<ul class="org-ul">
<li>Substâncias com menor TE saem no topo</li>
<li>Substâncias com maior TE saem na base</li>
</ul>

<p>
<b>Diferença entre destilações:</b>
</p>
<ul class="org-ul">
<li><b>Simples:</b> Sólido + líquido OU líquidos com TEs muito diferentes</li>
<li><b>Fracionada:</b> Líquidos miscíveis com TEs próximas</li>
</ul>
</div>
</div>

