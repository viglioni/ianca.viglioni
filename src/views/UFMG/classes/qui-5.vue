<h4 id="orgec8b06c"><span class="section-number-4">1.5.21.</span> Dicas para a Prova</h4>
<div class="outline-text-4" id="text-1-5-21">
<ol class="org-ol">
<li><b>Identifique o tipo de mistura:</b> Homogênea ou heterogênea?</li>
<li><b>Identifique os estados físicos:</b> Sólido-sólido, sólido-líquido, líquido-líquido?</li>
<li><b>Pense na propriedade diferente:</b> Tamanho? Densidade? Solubilidade? TE? Magnetismo?</li>
<li><b>Processos combinados:</b> Problemas complexos geralmente requerem vários processos</li>
<li><b>Destilação:</b> Simples (TEs muito diferentes) vs Fracionada (TEs próximas)</li>
<li><b>Funil de separação:</b> Sempre para líquidos imiscíveis</li>
</ol>
