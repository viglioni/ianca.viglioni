<div class="outline-text-4" id="text-1-5-10">
<p>
<b>Princípio:</b> Separar sólido de líquido usando material poroso
</p>

<p>
<b>Quando usar:</b> Sólido insolúvel + líquido
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Coar café</li>
<li>Filtrar água</li>
<li>Separar areia da água</li>
<li>Tratamento de água e esgoto</li>
</ul>

<p>
<b>Equipamentos:</b>
</p>
<ul class="org-ul">
<li><b>Simples:</b> Coador, papel de filtro, tecido</li>
<li><b>Laboratório:</b> Funil + papel de filtro + suporte</li>
</ul>

<p>
<b>Processo:</b> 1. Mistura passa pelo filtro 2. Sólido fica retido (resíduo) 3. Líquido passa (filtrado)
</p>

<p>
<b>Tipos especiais:</b>
</p>
</div>

