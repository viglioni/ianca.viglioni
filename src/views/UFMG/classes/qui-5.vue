<div id="outline-container-org695da3f" class="outline-4">
<h4 id="org695da3f"><span class="section-number-4">1.5.3.</span> Processos de Separação de Misturas Heterogêneas</h4>
</div>
<div id="outline-container-org3980807" class="outline-4">
<h4 id="org3980807"><span class="section-number-4">1.5.4.</span> 1. Catação</h4>
<div class="outline-text-4" id="text-1-5-4">
<p>
<b>Princípio:</b> Separação manual dos componentes
</p>

<p>
<b>Quando usar:</b> Componentes sólidos grandes e facilmente distinguíveis
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar feijões estragados</li>
<li>Retirar impurezas de grãos</li>
<li>Separar pedras de arroz</li>
<li>Reciclagem (separar plásticos, metais, vidros)</li>
</ul>

<p>
<b>Vantagens:</b> Simples, não requer equipamentos <b>Desvantagens:</b> Lento, não serve para partículas pequenas
</p>
</div>
</div>

<div id="outline-container-org9599d0f" class="outline-4">
<h4 id="org9599d0f"><span class="section-number-4">1.5.5.</span> 2. Peneiração (Tamisação)</h4>
<div class="outline-text-4" id="text-1-5-5">
<p>
<b>Princípio:</b> Separação por diferença de tamanho usando peneiras/tamises
</p>

<p>
<b>Quando usar:</b> Sólidos com partículas de tamanhos diferentes
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar areia de pedregulhos</li>
<li>Peneirar farinha</li>
<li>Separar grãos na agricultura</li>
<li>Construção civil (separar agregados)</li>
</ul>

<p>
<b>Equipamento:</b> Peneira ou tamis (malha com furos de tamanho definido)
</p>

<p>
<b>Vantagens:</b> Simples e eficiente para diferenças de tamanho <b>Desvantagens:</b> Só funciona para sólidos de tamanhos diferentes
</p>
</div>
</div>

<div id="outline-container-org86b88e0" class="outline-4">
<h4 id="org86b88e0"><span class="section-number-4">1.5.6.</span> 3. Ventilação (Levigação)</h4>
<div class="outline-text-4" id="text-1-5-6">
<p>
<b>Princípio:</b> Separação por diferença de densidade usando corrente de ar
</p>

<p>
<b>Quando usar:</b> Sólido mais leve + sólido mais denso
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar cascas de grãos</li>
<li>Separar palha de cereais</li>
<li>Limpeza de grãos de café</li>
<li>Separar areia (mais densa) de serragem (menos densa)</li>
</ul>

<p>
<b>Processo:</b> Corrente de ar arrasta o material menos denso
</p>
</div>
</div>

<div id="outline-container-org29e981c" class="outline-4">
<h4 id="org29e981c"><span class="section-number-4">1.5.7.</span> 4. Flotação</h4>
<div class="outline-text-4" id="text-1-5-7">
<p>
<b>Princípio:</b> Separação por diferença de densidade usando líquido
</p>

<p>
<b>Quando usar:</b> Sólido menos denso + sólido mais denso, usando água
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar plástico de vidro em reciclagem</li>
<li>Separar serragem de areia</li>
<li>Mineração (separar minérios)</li>
</ul>

<p>
<b>Processo:</b> 1. Adiciona-se água à mistura 2. Material menos denso flutua 3. Material mais denso afunda 4. Coleta-se separadamente
</p>

<p>
<b>Aplicação industrial:</b> Enriquecimento de minérios com uso de reagentes que alteram a densidade superficial
</p>
</div>
</div>

<div id="outline-container-org5ac4714" class="outline-4">
