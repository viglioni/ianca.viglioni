<div id="outline-container-org0146603" class="outline-4">
<h4 id="org0146603"><span class="section-number-4">1.5.9.</span> 6. Dissolução Fracionada</h4>
<div class="outline-text-4" id="text-1-5-9">
<p>
<b>Princípio:</b> Usar um solvente que dissolve apenas um dos componentes
</p>

<p>
<b>Quando usar:</b> Um sólido solúvel + um sólido insolúvel no mesmo solvente
</p>

<p>
<b>Exemplos:</b>
</p>
<ul class="org-ul">
<li>Separar sal (solúvel) de areia (insolúvel) usando água</li>
<li>Separar açúcar de serragem</li>
</ul>

<p>
<b>Processo:</b> 1. Adiciona-se o solvente (ex: água) 2. Um componente dissolve, outro não 3. Filtra-se para separar o sólido insolúvel 4. Evapora-se o solvente para recuperar o sólido dissolvido
</p>
</div>
</div>

