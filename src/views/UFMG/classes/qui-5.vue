<div id="outline-container-orgac154fa" class="outline-4">
<h4 id="orgac154fa"><span class="section-number-4">1.5.22.</span> Conceitos-Chave para Memorizar</h4>
<div class="outline-text-4" id="text-1-5-22">
<p>
<b>Misturas Heterogêneas (sólido-sólido):</b>
</p>
<ul class="org-ul">
<li>Catação: manual</li>
<li>Peneiração: tamanho</li>
<li>Ventilação/Levigação: densidade + ar</li>
<li>Flotação: densidade + água</li>
<li>Separação magnética: magnetismo</li>
<li>Dissolução fracionada: solubilidade</li>
</ul>

<p>
<b>Misturas Heterogêneas (sólido-líquido):</b>
</p>
<ul class="org-ul">
<li>Filtração: porosidade</li>
<li>Decantação: densidade + repouso</li>
<li>Centrifugação: densidade + rotação</li>
</ul>

<p>
<b>Misturas Heterogêneas (líquido-líquido):</b>
</p>
<ul class="org-ul">
<li>Decantação com funil de separação</li>
</ul>

<p>
<b>Misturas Homogêneas:</b>
</p>
<ul class="org-ul">
<li>Evaporação: perde o líquido</li>
<li>Destilação simples: recupera o líquido (TEs muito diferentes)</li>
<li>Destilação fracionada: líquidos com TEs próximas</li>
</ul>
</div>
