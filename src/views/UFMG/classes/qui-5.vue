<div id="outline-container-org962286b" class="outline-4">
<h4 id="org962286b"><span class="section-number-4">1.5.18.</span> Processos Combinados</h4>
<div class="outline-text-4" id="text-1-5-18">
<p>
Na prática, muitas separações requerem combinação de processos:
</p>

<p>
<b>Exemplo 1: Sal + Areia + Água</b> 1. Dissolução fracionada (água dissolve sal, não dissolve areia) 2. Filtração (separa areia) 3. Evaporação ou Destilação (recupera sal da água)
</p>

<p>
<b>Exemplo 2: Água barrenta</b> 1. Decantação (remove sólidos maiores) 2. Filtração (remove sólidos menores) 3. Cloração/purificação (tratamento químico)
</p>

<p>
<b>Exemplo 3: Petróleo bruto</b> 1. Decantação (remove água e impurezas) 2. Destilação fracionada (separa componentes)
</p>
</div>
</div>

